                            <p class="text-dark text-justify text-4">On the 1<sup>st</sup> of December, 1994 seven scouts
                                from Colombo
                                District were summoned to Scout Head Quarters by the Scout Commissioner and informed that
                                they had been selected to represent Sri Lanka at the 2<sup>nd</sup> Asia Pacific –
                                6<sup>th</sup> New Zealand
                                Scout Camp to be held at Hannas Bay Rotorua in North Island N.Z. from the 2nd to the
                                11<sup>th</sup> of
                                January, 1995.</p>



                            <p class="text-dark text-justify text-4">Lake Rotorua is located 240 kilometers south of
                                Auckland in the centre of the N.Z. thermal region at an altitude of 280 meters within close
                                proximity to surf beaches, rugged mountains, exotic forests, rivers and waterfalls, During
                                January the climate here is cool and pleasant during day followed by cold nights.</p>



                            <p class="text-dark text-justify text-4">All Venture Scouts and Senior Scouts from around the
                                world between the ages of 15-18 male and female were eligible for selection to participate
                                in this camp.
                            </p>

                            <p class="text-dark text-justify text-4">The theme of the 6th N.Z. Venture was “We will Rock
                                you” and the Camp programme was to operate in 2 phases.
                            </p>

                            <p class="text-dark text-justify text-4">
                                <span class="font-weight-semi-bold">Phase I:</span> A four day duration Expedition
                                withinRotorua region.
                            </p>

                            <p class="text-dark text-justify text-4">
                                <span class="font-weight-semi-bold">Phase II:</span> Four days at a site in Hannas Bay with
                                emphasis on fun and friendship and short tours within the region and on-site activities.
                                Expedition options included canoeing, caveing, farm life, Maori Heritage, wind surfing,
                                skydiving, while venture base activities from Hannas Bay included Forest hiking, mountain
                                climbing and white water rafting. Overseas participants were to conclude their stay in N.Z.
                                with a home hospitality stay with a Scout family.
                            </p>

                            <p class="text-dark text-justify text-4">The Sri Lanka contingent that left Katunayake on the
                                morning of 31st December, 1994 included President’s Scouts namely A.R. Gunawardana,
                                Contingent Leader Dinuk Aluvihare from 16<sup>th</sup> Colombo S. Thomas’ College and six other 1st
                                Class Senior Scouts from 42nd Colombo Royal College. The Contingent arrived in Auckland N.Z.
                                on 1<sup>st</sup> January, 1995 after breaking Journey at Singapore, Sydney and seeing the new year
                                1995 down high up at 30,000 feet some over the pacific ocean.</p>

                            <p class="text-dark text-justify text-4">We were welcomed by the venture organizers at the
                                airport and after sleeping over night at a scout A.D.C.’s home. Early next morning the Sri
                                Lankan contingent met other scouts, who had arrived in Auckland, at an appointment place to
                                board buses for Lake Rotorua campsite which was reached sometimes in the mid afternoon.</p>

                            <p class="text-dark text-justify text-4">Soon after the opening ceremony our contingent was
                                divided into 25 groups. The 2 scouts from 16th Colombo came into a unit of ten comprising 5
                                boys and 5 girls – 2 Sri Lankans, 6 New Zealanders and 2 Japanese.</p>

                            <p class="text-dark text-justify text-4">After one night’s camping at the Base Camp our unit
                                moved out in the morning to pitch camp on a farm 50 miles away. We took with us all camping
                                equipment and food from base camp. Each pitched a one man tent for him / herself while a
                                central kitchen tent also provided for the quartermaster’s store. Food stocks were plenty –
                                chicken, beef, mutton, bread, apples, oranges, pears and chocolates were supplemented by
                                fresh vegetables milk etc. from the farm.</p>

                            <p class="text-dark text-justify text-4">We camped at this site for 5 days. The farm land of
                                undulating was extensive and ended by a forest; miles away. The farmer and family tended the
                                milk cows, sheep and horses assisted by their trained dogs who hunted their own food when
                                hungry. We spent our time horse riding, canoeing in a lake nearby, hiking on the mountain,
                                trails, motor cycling and talking late into the nights starry night sky by the warm
                                campfires.</p>

                            <p class="text-dark text-justify text-4">The nights were freezing cold and solar bags provided
                                warm water for showering during the day when a burning sun shone in a clear blue sky.
                            </p>

                            <p class="text-dark text-justify text-4">After returning back to the Base Camp, we continued to
                                remain within our unit sleeping under canvas and spending the days on activities such as
                                rafting, canoeing, wind surfing, mountaineering and most of all meeting scouts from other
                                countries and making friends.</p>

                            <p class="text-dark text-justify text-4">For entertainment the organizers took us on a tour to a
                                wildlife park, aquatic centre, fun park, shopping, films and discos.
                            </p>

                            <p class="text-dark text-justify text-4">Six hundred and fifty boys and girls from around the
                                world the representing New Zealand, Australia, Japan, U.S.A., U.K., Korea, Sri Lanka and
                                many Pacific Islands participated in a spirit of friendship, Scouting brotherhood and broke
                                Camp on the 11<sup>th</sup> of January ’95 to return to their homelands with pleasant memories and a
                                host of new friends.
                            </p>

                            <p class="text-dark text-justify text-4">The Sri Lankan Contingent left Camp to go on their
                                different ways before returning home. Dinuk Aluvihare and I Stopped over in Australia
                                visiting friends and relations. In Sydney we met one of our College Scout from 16<sup>th</sup> Colombo
                                Chintaka Galapathy who was studying in Australia. He gave us a gift of a tent to take back
                                to our College Scout Troop. It was nice to meet a former troop friend in so distant a place
                                from home.
                            </p>

                            <p class="text-dark text-justify text-4">The Venture Camp apart from being ten carefree days of
                                fun with so many adventurous things to do and making friends from all parts of the world,
                                also instilled a deep sense of responsibility and built confidence in us which would have
                                been difficult to acquire without an overseas experience of a Venture Camp of this nature.
                            </p>

                            <p class="text-dark text-justify text-4">We thank the Scout Association of New Zealand, their
                                organizers and officials for their genial hospitality and for having gladly looked after us;
                                the A.D.C. Auckland Mr. Philip Dicky for having hosted us at his home for the post camp home
                                hospitality, all our local sponsors and the College who contributed generously towards our
                                travel expenses and made this memorable trip a reality.
                            </p>

