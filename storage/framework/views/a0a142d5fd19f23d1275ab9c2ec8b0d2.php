                            <h4 class="font-weight-semi-bold">The Recent Times: 1968-Present:</h4>

                            <p class="text-dark text-justify text-4">This era was mainly taken over by one of the giants in
                                the Scout Movement of Sri Lanka – Mr. Daniel Edirisinghe, who rendered a yeoman service to
                                the Troop as Group Scout Leader from 1970 to 1982 and from 1986 till 2005. He was initially
                                an Asst. District Commissioner of Colombo and then went on to be a Headquarters
                                Commissioner. He was later promoted as an Asst. Chief Commissioner and finally was made the
                                National Training Commissioner of the Sri Lanka Scout Association. He is also a Leader
                                Trainer. The Troop is indeed privileged to have been under the guidance of such profound
                                individuals who have taken major initiatives in bringing Scouting in Sri Lanka to what it is
                                today. Mr. M.N.R. Fernando, who went on to be District Commissioner of Colombo and Leader
                                Trainer of S.L.S.A., was beside him as Scout Leader to assist in taking the Troop from
                                strength to strength. Both of their ventures have immensely helped to bring Scouting in the
                                College to what it is, today. Mr. A.L. Victor also assisted the Troop from time to time as
                                the Group Scoutmaster.</p>

                            <img src="<?php echo e(asset(path: 'images/Daniel-Sir-Portrait-234x300.jpg')); ?>"
                                class="img-fluid float-end p-2" alt="Daniel-Sir-Portrait-234x300" />

                            <p class="text-dark text-justify text-4">Being acclaimed as one of the most active and prominent
                                Scout Troops in the island, it could be clearly seen that Scouting at S. Thomas’ College,
                                Mount Lavinia has developed into what is a productive entity over the years in the school
                                from which productive citizens have been the fruitful product.</p>




                            <p class="text-dark text-justify text-4">Hence, naturally the membership of Scouting at College
                                increased immensely and the Scouts of College started organising activities which benefitted
                                the social community at large. Our Scouts also took part in all possible international
                                activities which included World Jamborees and Conferences.</p>

                            <p class="text-dark text-justify text-4">After a lapse of 10 years, the Rover Crew was revived
                                in 1980 as the initially formed crew ceased to exist. The Troop won the C.P.J. Shield in
                                1987 and the Mayor’s Flag competition in 1989 for the first time. Meanwhile, the present
                                Scout Room was donated and opened in 1987 by Bernard Boteju Esq.</p>

                            <p class="text-dark text-justify text-4">By now the Junior Troop consisted of five patrols –
                                namely Eagles, Leopards, Kingfishers, Bears and the Sharks (Inaugurated in 1989) – the
                                latest addition. The Senior Troop had three patrols namely, Gamunu, Wijeya and Parakrama.
                                The 1990s, was another brilliant era for Scouting, as many projects were undertaken which
                                were of large scale.</p>

                            <p class="text-dark text-justify text-4">The Troop at this stage started doing projects along
                                with the Girl Guides, who are their counterparts. The first project of this nature was
                                “Beats of Scouting” in 1995 which brought together fellow guide companies from the Colombo
                                district. From there on various projects of similar nature were conducted – to name a few,
                                Tidal Wave (1996), Coastal Winds (1997), Sandstorm (1998), Déjàvu (1999) Cyclone (2000) and
                                Tribal Campfire (2002). The peak of organizing such projects was when the Troop organized
                                “Tribe Out” in 2002 which was held at Lee Dassanayake Training Centre in Mirigama which was
                                a camp with the Guides. This went on to show the bonds and the trust that the two movements
                                had built over the past years.</p>

                            <p class="text-dark text-justify text-4">Meanwhile, the “Scout Dunk” – a novel invention of the
                                Troop became a highlight of most carnivals in Colombo and continues to mesmerise the crowds
                                even now.</p>

                            <p class="text-dark text-justify text-4">The Scouts of S. Thomas’ Mount Lavinia have
                                continuously been able to mould Scouting in Sri Lanka for the past years by organizing
                                unique projects which were of high value. Such a major project of this nature carried out by
                                the Troop was in 2004, when it organized “Tribal Craft”, a huge Scout exhibition which was
                                not only a thumping financial triumph but also an exhibition which put Thomian Scouts on the
                                map as one of the most active and capable Troops in the island. At this exhibition, the
                                official website of the Troop was launched, making it one of the initial Troop websites to
                                be launched in Sri Lanka. By now, the Troop was famously known as “The Tribe of the Evening
                                Star” which has its roots in the location where the present Scout Room is located (Adjoining
                                the beach)!</p>


                            <img src="<?php echo e(asset(path: 'images/Wathsala-Wijewickrema-247x300.jpg')); ?>"
                                class="img-fluid float-end p-2" alt="Wathsala-Wijewickrema-247x300" />

                            <p class="text-dark text-justify text-4">Many Scouts from the Troop were able to represent Sri
                                Lanka in a lot of international events in the past few years. The most recent venture of
                                this calibre was the 22nd World Scout Jamboree held in 2011 in Sweden where 4 Scouts
                                represented Sri Lanka.</p>

                            <p class="text-dark text-justify text-4">The Troop produces a considerable amount of President’s
                                Scouts each year, thus proving that they are not only good in practicality but also in
                                theory. At present the Troop is supported by the very much active Group Committee which is
                                presently headed by Mr. Senaka de Fonseka where past Scouts, parents and well-wishers form
                                this main body to support the Troop financially. Meanwhile the instructors render a lot of
                                help to the present Scouts in their daily activities as well.</p>

                            <p class="text-dark text-justify text-4">With regard to taking part in the competitions
                                organized by the district, the Troop has done exceptionally well over the past years. In the
                                district Scout competitions held last year (2013), the Troop was able to be champions in
                                these competitions. This made the Troop to retain the Mayor’s Flag for the 8th consecutive
                                year and the C.P. Jayawardena Shield for the 5th consecutive year. When considering both the
                                competitions as a whole the Troop has been able to win both the competitions for 5 years in
                                a row from 2009-2013. Meanwhile in the recently started competition for the Cub Scouts, the
                                C.M.P. Wanigathunga competition, the Troop has been able to win all 7 competitions held
                                since 2007.</p>

                            <p class="text-dark text-justify text-4">Furthermore the Troop has been successful in conquering
                                the sports arena in the means of winning the Sports Galas and the Swimming Meets organized
                                by the district over the past few years. The Troop won both the Sports Gala and the Swimming
                                Meet in 2012.</p>

                            <p class="text-dark text-justify text-4">When referring to the financial triumphs that the Troop
                                has had, the Troop has been able to be the highest collectors in the Service and
                                Relationship Week (a.k.a. Job Week) in the Colombo district for the last many years. Thus by
                                all these facts it is clear that the Thomian Scout community is by no means an anachronism
                                but an active entity.</p>

                            <p class="text-dark text-justify text-4">To further extend this level and to celebrate 100 years
                                of Scouting in our country, as the most recent venture, the Scouts organized a Scout
                                campfire in the belief that this would be yet another extravagant experience with a
                                difference, for all participants and spectators. This project was a Grand Campfire titled
                                “Centennial Flames 2012” which was of a high magnitude so as to revive the phenomenon of
                                organizing large-scale projects. The project was organized with the main objective of
                                creating a better understanding between Scouts and society & increasing the bonds with
                                fellow Scouts and guides while celebrating the Centenary of Sri Lankan Scouting. The main
                                theme behind the project was “Celebrating 100 years of selfless service…”. This campfire was
                                thus concluded successfully on the 17th of February 2012 and brought together Scouts, Guides
                                and officials from all over the district.</p>

                            <p class="text-dark text-justify text-4">To further extend this level and to celebrate 100 years
                                of Scouting in our country, as the most recent venture, the Scouts organized a Scout
                                campfire in the belief that this would be yet another extravagant experience with a
                                difference, for all participants and spectators. This project was a Grand Campfire titled
                                “Centennial Flames 2012” which was of a high magnitude so as to revive the phenomenon of
                                organizing large-scale projects. The project was organized with the main objective of
                                creating a better understanding between Scouts and society & increasing the bonds with
                                fellow Scouts and guides while celebrating the Centenary of Sri Lankan Scouting. The main
                                theme behind the project was “Celebrating 100 years of selfless service…”. This campfire was
                                thus concluded successfully on the 17th of February 2012 and brought together Scouts, Guides
                                and officials from all over the district.</p>

                            <p class="text-dark text-justify text-4">In 2013, a scholarship was formed to appreciate a
                                worthy Scout in the name of Mr. W.I. Muttiah which would be awarded each year. The Rover
                                Crew was also reformed in January 2013.</p>

                            <p class="text-dark text-justify text-4">When referring to the financial triumphs that the Troop
                                has had, the Troop has been able to be the highest collectors in the Service and
                                Relationship Week (a.k.a. Job Week) in the Colombo district for the last many years. Thus by
                                all these facts it is clear that the Thomian Scout community is by no means an anachronism
                                but an active entity.</p>

                            <p class="text-dark text-justify text-4">It is worthy to mention the name of Mr. Upali Salgado,
                                probably the oldest living Thomian Scout who is presently in his early eighties. He is a
                                Wood Badge holder and went on to be a Headquarters Commissioner. He is also responsible for
                                the authorship of the book published by the C.L.A. for the 1957 Golden Jubilee Jamboree.</p>


                            <p class="text-dark text-justify text-4">From an activity that was part and parcel of Cadetting,
                                Scouting was eventually formalised and has grown over the past century to be one of the most
                                active and enthusiastic activities of the school. “Scouting is life, the rest is just
                                details” is a premise that all Thomian Scouts live by which truly depicts what Thomian
                                Scouts truly stand for – uniqueness, skill and perfection.</p>

