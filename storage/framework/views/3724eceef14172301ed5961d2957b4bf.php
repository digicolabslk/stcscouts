                            <br>
                            <p class="text-dark text-justify text-4">A big step forward in Scouting at S.T.C. was the
                                formation of the Rover Scout Crew in 1965, to cater to the needs of Scouts who had left
                                College in but wished to continue to support the Troop. Mr. Lionel De Silva, Scout Field
                                Commissioner, volunteered to be the first Rover Scout Leader of this crew. In 1968, the
                                Coming-of-Age Rover Moot was held at the Small Club Grounds organised by the 16th Colombo
                                Rovers.</p>



