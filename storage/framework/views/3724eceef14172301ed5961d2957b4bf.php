                            <p class="text-dark text-justify text-4">The standard of Scouting too improved after this new
                                addition and this was now seen as an activity to be taken up by every student, especially as
                                Scouting promoted self development and personality development which was specifically needed
                                in a post-war era. In 1949, Mr. Maurice A. De Silva became the first King’s Scout and Mr.
                                N.K. Mutucumarana became the first Queen’s Scout of the Troop in 1951. Both of them assisted
                                the Troop to continue the standard of at least having one Queen’s Scout per year which bore
                                fruits in the coming years. Some of the many Queen’s Scouts who followed were K.
                                Thiyagarajah (1952), N.C. de S. Samarasinghe (1953), M.R. Kumarasamy (1954), L.W. Rasiah
                                (1956) and J.B.G. Jonson (1956). C.D. Jayakody and R.C. Wanigatunga (1957) were also among
                                then both of whom represented the country at the 9th World Scout Jamboree held in Birmingham
                                U.K. This list of Scouts achieving excellence in Scouting at 16th Colombo continues to date
                                only being replaced by the President’s Scout Award of recent times.</p>

