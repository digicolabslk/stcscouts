                            <h4 class="font-weight-semi-bold">Post-war Scouting: 1946-1968:</h4>

                            <p class="text-dark text-justify text-4">The war ceased to exist in 1946, and in January a
                                scattered portion of the College was re-instated in Mount Lavinia. At the request of the
                                Warden at the time, Mr. W.I. Muttiah undertook to organise the Scout Troop in Mount Lavinia,
                                as Group Scoutmaster of 16th Colombo Scout Troup. Mr. Muttiah is looked upon as an able
                                individual who had a perfect personality in terms of being a Scout Leader, who taught the
                                Scouts at the time that it was quality of work and skills that mattered and not the number
                                of badges a Scout had. As there was no Cadet Corps as yet, there were quite a number of boys
                                who wished to join Scouting. Scouts who were trained under Dr. Hayman in Gurutalawa played
                                an anchor role in re-building the Troop. C.H. Mendis who was among them was the first to
                                qualify as a First Class Scout and was appointed the first Troop Leader of this “fresh”
                                Troop which consisted of six patrols (the Leaders of which were C.J. Hapugoda, S. Kangari,
                                U.K. Salgado, S.C.M. Fernando and M.A. de Silva). Mr. Henry Paul took over duties as the new
                                Scoutmaster.</p>


                            <p class="text-dark text-justify text-4">At that time the Scout Troop had no funds at all to its
                                credit and no material of any kind – not even a piece of string! The first opportunity to
                                earn some money came the following year when the Troup undertook the Sale of Ice Cream at
                                the Royal Thomian Cricket match. From such humble beginnings, the Troup continued to
                                progress in spite of many ups and downs. A regular source of income became the Annual Scout
                                Concert, which was entirely a 16th Colombo Scout affair, produced and directed by the G.S.M.
                                Mr. Muttiah, and all the plays without exception were provided by members of the Troop and
                                the Cub Pack, who were also responsible for putting up the stage and seeing to the sale of
                                tickets. The first of such Scout concerts was said to have been held in 1948 while the Annul
                                Scout Concert in 1956 brought a handsome return of Rs. 634, the highest to date.</p>


                            <img src="<?php echo e(asset(path: 'images/Mr-WI-Muttiah-257x300.jpg')); ?>" class="img-fluid float-end p-2" alt="" />

                            <p class="text-dark text-justify text-4">Annual Parents’ Days were organised in this era which
                                was a highlight event in the school calendar. The first one of these was held in 1948 and
                                was well attended. In 1949, the “Chips-for-Jobs” (a.k.a. Job Week) campaign was launched by
                                the C.L.A. to help the Troops to earn their own funds to run their Scout activities. The
                                highest earnings of the Troop were in 1957 – the Birth Centenary year of the Founder and the
                                Jubilee Year of World Scouting.</p>

                            <p class="text-dark text-justify text-4">With all these initiatives, the Troop was able to
                                manage its own income and expenditures, thus being the only entity in College to operate
                                without financial support from College. This is the case even today as well. To further
                                strengthen this status (in 1956) and to accommodate many boys now anxious to become Scouts
                                following a very successful National Scout Week campaign in September – a second Troop was
                                formed with Mr. R.J. Aruliah as Scoutmaster of the “A” Troop and Mr. V. Vitharana as
                                Scoutmaster of “B” Troop.</p>

                            <p class="text-dark text-justify text-4">Around the same time, the first ever Group Committee
                                was formed, with Mr. C.H. Davidson as the Chairman and Mr. R.J. Aruliah as the Honorary
                                Secretary along with some senior members of the staff and parents. Mr. C.H. Davidson as
                                Chairman fulfilled a long felt need of getting the Scouts of 16th Colombo official
                                recognition at S.T.C. and the Queen’s Scout Badge was accepted as a qualification when
                                considering boys for position of responsibility in their House or School.</p>

                            <p class="text-dark text-justify text-4">One of the first projects that the Group Committee
                                undertook was to build a new Scout Room to accommodate the increased number of Scouts. The
                                Warden at the time, Rev. Canon R.S. De Saram was generous enough to allocate a portion of
                                land for this, and the new building came up within the span of a year with a lot of hands on
                                assistance from the Scouts in whichever way they could, under guidance of Mr. G.E. Jayakody
                                as Director of Buildings. The New Scout Room was declared open by Mr. Charles Dymoke-Green,
                                the Asst. Chief Commissioner on Parents’ Day on the 5th of March 1958.</p>

