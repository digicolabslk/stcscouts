                                <p class="text-dark text-justify text-4">The 16th Colombo Cub Pack, where the following
                                    records attribute to, reveal how the Cub Pack has taken a pioneering role, since in
                                    inception in 1922, to produce all-rounded Cub Scouts who would go on to be profound
                                    Scouts later.</p>
                                <p class="text-dark text-justify text-4">Initially, the Rev. J.Y. Barnabas, Headmaster of the
                                    Lower School, was a keen supporter of Scouting and he was very anxious that a Cub Pack
                                    be started for smaller boys. He himself looked after the Cubs in pre-war days, taking
                                    them on picnics, excursions and camps. At his request Mrs. D. Jansz, who had done Cub
                                    work earlier, started a Cub Pack in I950. The Cubs gave an item at the Scout Concert
                                    that year and did their share of “Chips for Jobs”. With the help of one or two members
                                    of the Lower school staff Mrs. Jansz did her work efficiently till she gave it up for
                                    reasons of health. The responsibility was then taken over by Miss N. Jayasuriya and Miss
                                    Unantenne who looked after the Pack the following year who were helped in the third term
                                    by S. Balasubramaniam, one of the Senior Scouts.</p>
                                <p class="text-dark text-justify text-4">Mrs. J. Jacob and Miss A.C. Perera took over in
                                    1952, and under their leadership the Pack increased in numbers and efficiency. Mr. N.B.
                                    Arputharaj was also a prominent figure who was involved in Cub Scouting in College from
                                    1951-1957. When Miss Perera left, Mrs. Jacob had to give up owing to pressure of work,
                                    while a few others looked after the Pack for short periods till Mr. K.G. Leo de Silva
                                    qualified for his warrant as Cub Scouter and took full charge. He was assisted by Miss
                                    Olga de Alwis and Miss D. Abeywardene.

                                </p>
                                <p class="text-dark text-justify text-4">After Miss Olga De Alwis left, Mrs. D.D. Sri Murugan
                                    took over her responsibility. In 2000, both Mrs. D.D. Sri Murugan and Mrs. W.
                                    Wijewickrema were made the Teachers-in-Charge of Cub Scouting. Mrs. Sri Murugan is also
                                    the first lady Wood Badge holder in Scouting at S.T.C. From thereon, Cub Scouting
                                    emerged as a prominent activity for the students of the Lower School. Around the same
                                    time, the Colombo District Cub Scout Band was formed by the Cub Pack which was a great
                                    leap for Cub Scouting in the District. In the mean time, the present Cub Scout room was
                                    declared open in 2002, which was a donation by Mr. Anurath Abeyratne, a loyal parent of
                                    College. The Cubs also had the opportunity of taking part in the production of the
                                    official C.D. and song cassette for the 6th National Jamboree held in 2002.</p>
                                <p class="text-dark text-justify text-4">In 2003, Mrs. W. Wijewickrema was appointed the
                                    Asst. District Commissioner of Cubs (Colombo), and the following period oversaw the
                                    College Cub Pack being majorly involved in more activities with the District. The first
                                    ever District Christmas Carols programme was organised by the Cub Pack in 2003, which
                                    still continues to this day. Furthermore the District Cub Scout and Scout Swimming Meet
                                    was restarted in 2004 and was organised by the 16th Colombo Scout Group. In 2005, 7 Cubs
                                    from College had the privilege of attending the 25th A.P.R. Jamboree held in Thailand,
                                    which was the first venture of the sort.</p>
                                <p class="text-dark text-justify text-4">When referring to the social service aspect of the
                                    Cubs, it is worthy of note that the Annual Christmas Programme organised by the Cubs is
                                    organised in collaboration with the Deaf and Blind School in Ratmalana where the Cubs
                                    get involved in the donation of much needed items for them. The Pack also engages in
                                    contributing various provisions for orphanages, and other needy Scout Troops.</p>
                                <p class="text-dark text-justify text-4">In the field of maintaining a high standard of
                                    commitment and dedication, the Cubs have proven the same by conquering the competitions
                                    organised for them by the District. The Cub Pack has been able to win all the
                                    competitions of the C.M.P. Wanigathunga Flag Competition held to reward the Best Cub
                                    Pack in the District, since its inception in 2007.</p>
                                <p class="text-dark text-justify text-4">At present the Cub Pack consists of 6 Packs, with a
                                    total of more than 230 Cubs. The Pack also produces 30-40 Gold Star winners every year,
                                    which is quite remarkable. Hence in all aspects it could be seen how a systematic
                                    approach has paved way for considerable achievements which is also incorporated with
                                    developing the character of all the involved, in the past 90 years of Cub Scouting at S.
                                    Thomas’ College.</p>
