                            <p class="text-dark text-justify text-4">Bellana Vidanalage Rex Jayasinha, famously known as Rex
                                Jayasinha was a product of S. Thomas’ College, Mount Lavinia who went on to become the Chief
                                Commissioner of Sri Lankan Scouting.</p>

                            <p class="text-dark text-justify text-4">He was born on the 14<sup>th</sup> of June 1931 during
                                a crucial
                                time period of the contemporary world. He attended S. Thomas’ Mount Lavinia from 1937 to
                                1949. He was one of the founding students of S. Thomas’ Guruthalawa when the institution was
                                temporarily transferred to Guruthalawa during the 2<sup>nd</sup> World War, when STC Mount
                                functioned
                                as a hospital…</p>

                            <p class="text-dark text-justify text-4">He went onto pursue his further studies at the
                                University of Peradeniya and received a B.A. (Special) in Geography in 1988.

                            </p>

                            <p class="text-dark text-justify text-4">He was a dedicated government servant and served at
                                various institutions holding various positions. He became the Chief Commissioner of the Sri
                                Lanka Scout Association in 1988.</p>

                            <p class="text-dark text-justify text-4">When referring to the contributions he made towards the
                                development of Scouting, he is remembered for proposing the idea of a “SAARC Jamboree”, and
                                also for implementing it. He proposed the idea of this at the Asia Pacific Regional Scout
                                Conference held in Australia in 1989. He saw the idea being immediately accepted by the
                                President of Sri Lanka at the time Hon. Ranasinghe Premadasa. Therefore, arrangements were
                                made to host the said jamboree at the Vihara Maha Devi Park in 1990. However, quite
                                unfortunately Mr. Rex Jayasinha did not see it through to the final implementation, as he
                                passed away on the 3<sup>rd</sup> of February 1990, a few weeks before the jamboree.</p>

                            <p class="text-dark text-justify text-4">He is most faithfully remembered for the immense
                                service that he rendered for the development of Sri Lankan Scouting.
