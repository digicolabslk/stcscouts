                            <h2 class="font-weight-semi-bold">From The Archives…</h2>
                            <h4 class="font-weight-semi-bold">The Beginnings: 1912-1946:</h4>

                            <p class="text-dark text-justify text-4">At present S. Thomas’ College, Mount Lavinia is one of
                                the finest educational institutions of the country. The College was originally inaugurated
                                in Mutwal in 1851 by the first Bishop of Colombo, Rt. Revd James Chapman and brought to its
                                present location in Mount Lavinia in 1918.</p>


                            <p class="text-dark text-justify text-4">From there on, the College took the leading role in
                                producing all-rounded citizens to the country, where Scouting as an activity, has played an
                                anchor role.

                            </p>

                            <p class="text-dark text-justify text-4">According to the oldest records available at College,
                                Scouting as an activity seems to have germinated and started in an unofficial way at S.
                                Thomas’ while the School was still at Mutwal.</p>

                            <p class="text-dark text-justify text-4">The College Magazine for August 1912 states under
                                “School News”:</p>

                            <p class="text-dark text-justify text-4">“The Junior Cadets or Boy Scouts attended the Empire
                                Day Parade. They look very smart in their uniforms and do credit to Mr. H.J. Wijesinghe who
                                is in charge. Their total strength is 130.”</p>

                            <p class="text-dark text-justify text-4">Furthermore, the College History written by Mr. W.T.
                                Keble also suggests that some activity relevant to Scouting was around in the 1900’s:

                            </p>
                            <p class="text-dark text-justify text-4">“The Cadets won the Inter-school sports this year as
                                well as the Physical Training Cup. They were also reinforced by Scouts, who were afterwards
                                called Junior Cadets”

                            </p>
                            <p class="text-dark text-justify text-4">Certainly Scouting was “officially” permitted according
                                to our records only in 1922 after the School had moved to Mount Lavinia with the formation
                                of the 1st Cub Pack by Mr. V.P. Cooke and the 1st proper Scout Troop by Mr. S.L.H. de Zylva
                                in that same year. Thus, although the 16th Colombo Scout Group came into being much later as
                                our extant historical records indicate, at least since August 1912 Scouting as an activity
                                was being experimented. Certainly it may not have been as popular as Cadetting that it was
                                initially a part of.</p>

                            <p class="text-dark text-justify text-4">Mr. W.I. Muttiah, the legendary Group Scoutmaster
                                (G.S.M.) at S. Thomas’, writing an account of Thomian Scouting in 1968 based on an earlier
                                account by Dr. R.L. Hayman, comments on this earliest reference to Scouting at S.T.C. as
                                thus:</p>

                            <div class="col-md-12 align-self-center p-static order-2 text-center">
                                <img src="{{ asset('images/Dr.-R.L.-Hayman-216x300.jpg') }}"
                                    class="img-fluid img-thumbnail img-thumbnail-no-borders rounded-0" alt="" />
                            </div>
                            <p class="text-dark text-justify text-4">“The first record of Scouts at S. Thomas’ College was
                                in 1912. In that year the C.C.B. (Perhaps misunderstanding the nature of Scouting) started
                                the Junior Cadets and called them “Scouts”. S.T.C. had a platoon of these “Scouts” from the
                                outset, and in 1917 they won the Athletic Shield and the Physical Training Cup. Soon after,
                                they were given their proper name of Junior Cadets.”
                            </p>

                            <p class="text-dark text-justify text-4">From the article of Mr. Wilson Muttiah it is clear that
                                whenever the existing records refer to “Scouts” or “Boy Scouts” in the years between 1912
                                and 1922, they were in fact referring to the “Junior Cadets”. It could also be argued that
                                as the School authorities at S. Thomas’ did not encourage Scouting, as it was felt it would
                                rival the Cadets which in turn allowed the Scouts to be active as Junior Cadets or as part
                                of the Cadets.</p>


                            <p class="text-dark text-justify text-4">In any event, the first true Scout Troop started by the
                                Headmaster Mr. S.L.H. de Zylva ceased to exist on his departure due to the fact that it was
                                not encouraged by the School and was revived for good only in 1928 with the advent of Mr.
                                W.T. Keble and Dr. R.L. Hayman who went on to take Scouting at S. Thomas’ to great heights
                                including encouraging Scouting at their Schools at Gurutalawa and Bandarawela in the Uva
                                hills in 1942. Dr. R.L. Hayman is also a distinguished Sub Warden of College who assumed
                                duties as Sub Warden in 1935. Dr. R.L. Hayman is a Wood Badge holder himself. Hence it is
                                clear that the Scout Group celebrates either a centenary or 90 years for Scouting at S.
                                Thomas’ as of 2012.</p>


                            <p class="text-dark text-justify text-4">During the first two and a half years from
                                inauguration, the members of the Troop were practically confined to boarders. Hence the
                                dayboys joined the neighbouring “Open” Scout Troops – namely 15th Colombo and so forth. In
                                1932, the day boys wished to have a Troop of their own and it was constituted with Mr. Carl
                                V. Cooke as the Scoutmaster. He was already Scoutmaster of 15th Colombo at the time. These
                                two Troops were titled “A Troop” and “B Troop” respectively.</p>


                            <p class="text-dark text-justify text-4">In 1933, the Troop won the All-round Shield for
                                Scouting at the annual C.L.A. (Colombo Local Association) competition, for the first time.
                                In the same year, five members attended the World Scout Jamboree at Godollo in Hungary.</p>

                            <p class="text-dark text-justify text-4">In 1935, Messrs. Hayman and Keble left to Gilwell for a
                                Training Course for Scouters. In their absence, Mr. Godfrey Faussett – the District
                                Commissioner at the time undertook to run the Troop. With his knowledge and skills in
                                leadership, the Troop was even strengthened in certain weak areas such as the Badgework
                                progress of the Scouts. In September same year Messrs. Hayman and Keble returned and Mr.
                                Keble took over as the Group Scoutmaster while Dr. Hayman became Scoutmaster. However Mr.
                                Faussett was to overlook the progress as long as possible. At the end of 1937, Mr. Keble
                                left the staff to start off the new S. Thomas’ Preparatory School in Colpetty.</p>

                            <p class="text-dark text-justify text-4">Later on, in 1940 the Scouts yet again won the C.L.A.
                                competition. The seniors won the C.L.A. Shield the following year. At the end of 1942, the
                                dark clouds of the war approached the country and the College had to be shifted to so many
                                other destinations as it was being used as a military hospital. However, due to the
                                unstinting efforts of Dr. Hayman, the Troop ran smoothly in Gurutalawa (Still as 16th
                                Colombo), at least until the war was over. Later on a new Troop in S.T.C. Gurutalawa was
                                registered at the Badulla Scout Association too.</p>

