                            <p class="text-center text-decoration-underline text-dark text-justify text-4">The Kingdom of the Evening Star</p>


                           <p class="text-center  text-dark text-justify text-4">Far far away surrounded by the mount of Lavinia,<br> 
                                    Was a kingdom so great you’d wanna fit in there…<br> 
                                    All great kingdoms have equally great kings,<br> 
                                   Who ruled his subjects and made them happy to the brim,<br>
                                   The freaky king (the tall ugly one in specs mind you) ruled
                                   this land for quite some time,<br>
                                   And finally thought to take a break and spend some dime<br>
                                   (thank God!),<br>
                                   So calling his lords to the great court of honour,<br>
                                   Discussed about the prospective affair of appointing another…<br>
                                </p>

                           <p class="text-center  text-dark text-justify text-4">Two lords stood above the rest,<br>
                                  One the leader of the flower garden and the other a pimple face…<br>
                                  Both of them were quite good at what they did “So you pick the right one” they said – and farewell did they bid,<br>
                                  After this the tall king questioned both of them –<br>
                                  And while doing so dropped his imperial pen…<br>
                                  The pimple faced one wasted no time to pick it up, so did he bend,<br>
                                  All hail the new king, sparks did he send…<br>
                                </p> 
                                   
                                   
                                <p class="text-center  text-dark text-justify text-4">The leader of the flower garden, heartbroken took it quite well ’cause when the new king extended his hand,<br>
                                  It was his turn to bend,<br>
                                  Since the dropping of the coconut on his empty head;<br>
                                  Wires did get crossed and to Dharman love letters did he send,<br>
                                  However he wasted no time while gone on job week,<br>
                                  While the others washed the car to the flower maid did he speak…<br>
                                </p>    


                                <p class="text-center  text-dark text-justify text-4">The new king needed a Sergeant-at-Arms,<br>
                                  ‘Cause the last one to sight was a quite a task,<br>
                                  So the new king turned to the ‘eL eNa fellow’,<br>
                                  Whose sister’s sight turned him to Jello,<br>
                                  ‘Cause since the day he was caught in the act,<br>
                                  She blackmailed him to say nothing about that…<br>
                                </p> 
                                
                                
                                 <p class="text-center  text-dark text-justify text-4">The lords set fire to V.V. land;<br>
                                  “Help!” called the maidens of the deserted land of V.V.,<br>
                                  ‘Cause to make a campfire their ideas were quite wavy…<br>
                                  Being the kind of lords who wouldn’t dream of braking the codes of chivalry,<br>
                                  Mounted their stallions and to the rescue they made it;<br>
                                  Once they got there to much of their dismay;<br>
                                  Found out there was quite a lot to do to make it a special day,<br>
                                  Once they were over with the fire, it was quite a show,<br>
                                  They were amazed ’cause such expertise existed didn’t they know…<br>
                                </p> 
                                
                                
                                 <p class="text-center  text-dark text-justify text-4">All this while the lords were quite happy and gay,<br>
                                  Which worried the imperial family as it may..<br>
                                  Time has come once again to get matters straight,<br>
                                  So find some fine ladies they sent the new king right away,<br>
                                  The new king wondered away and eventually got lost,<br>
                                  And finally ended at a Mirigama spot…<br>
                                </p> 


                                

                                <p class="text-center  text-dark text-justify text-4">After a lot of convincing he made up his mind,<br>
                                  And ended up at V.V. land, gosh what a great find (yeah right!),<br>
                                  The new king confronted the lands captain,<br>
                                  And whole heartedly poured out his intention,<br>
                                  Alas! Time has come to get rid of these menaces the captain thought,<br>
                                  Take them right away and keep them until their sought.<br>
                                </p> 


                                <p class="text-center  text-dark text-justify text-4">The Imperial Family,<br>
                                  The Vegetarian lord full of class,<br>
                                  The ambassador of the royal throne – Lasses and Cars he’d never pass,<br>
                                  His retirement the kingdom does mourn.<br>
                                </p> 


                                <p class="text-center  text-dark text-justify text-4">There’s another whose lord whose finger will be in his nose throughout the day,<br>
                                  Went to Triumph to make his fortune but found nothing to his dismay,<br>
                                  Because of his inefficiency Barbie gave him the boot,<br>
                                  Burning in his shoes the lad lost his sting for girls and ended way off route,<br>
                                  The shortest in the land got himself a pushbike (one with the motor that has to be pushed),<br>
                                  Giving big akki’s lifts up to the road made his day’s spike,<br>
                                  Much was he dismayed about his height ’cause the only thing long in him is his hair as you may sight…<br>
                                </p> 


                                <p class="text-center  text-dark text-justify text-4">The ex-sergeant-at-arms was a sea food junky,<br>
                                  Tried to get a date with Nata***, so did he get funky,<br>
                                  Since that day to every function did he go to the Audi of BC,<br>
                                  But never spotted each other ’cause both of them were quite dark you see…<br>
                                </p> 


                                <p class="text-center  text-dark text-justify text-4">The kingdom’s ex-maha kokiya had a game plan,<br>
                                  So all his cousins he enrolled in the kingdoms land,<br>
                                  Can’t blame the fellow for being a family man,<br>
                                  ‘Cause while on camp grub did he smuggle to the entire family band…<br>
                                </p> 


                                <p class="text-center  text-dark text-justify text-4">There were two others in the Imperial Family,<br>
                                  One huge gorilla who would stink like for an eternity ‘I will get married’ one day he says with much pride,<br>
                                  All we can say is “good luck” to his unfortunate bride.<br>
                                  Since the big boot he didn’t show his feelings to another,<br>
                                  So wound up calling a married woman to keep his heart on tire,<br>
                                  Everything went fine until the husband found out<br>
                                  Since that day to Apit and the Muslim babe never combined…<br>
                                </p> 



                                <p class="text-center  text-dark text-justify text-4">Profiles of the young lords;<br>
                                </p> 



                                <p class="text-center  text-dark text-justify text-4">There was another Pala from the konaka city,<br>
                                  Who had a thing for the shorthaired fagging missy,<br>
                                  Of course the new king was quite fond of her too,<br>
                                  But the Pala beat him and his classmate to rock his way through,<br>
                                  There’s another hard working lord who was quite happy and resembled a cow,<br>
                                  Who saw a skinny maiden and said “MOO” and then said “WOW”,<br>
                                  Later they would meet at the Council of British and two tickets would he buy;<br>
                                  The lady took the tickets, thanked him, and turned up with another guy…<br>
                                </p> 

                                 <p class="text-center  text-dark text-justify text-4">The balloon chest-ed lord who always played pucks,<br>
                                  And ended up getting the dreaded chicken pox,<br>
                                  Among his hobbies was to travel by bus even so may,<br>
                                  And imitate Leo deCaprio and be the jack of his day…<br>
                                </p> 


                                 <p class="text-center  text-dark text-justify text-4">This lord who got a trash can for a name<br>
                                  Tried his luck as it may to end up with a dame;<br>
                                  So one day he gathered his guts and to a maiden said “Hi”,<br>
                                  She returns a compliment and asks him “Gosh who’s that other guy!”<br>
                                </p> 


                                 <p class="text-center  text-dark text-justify text-4">The two new lords were quite tall,<br>
                                  One dark and the other fair as you may call,<br>
                                  One was quite good at singing and drawing,<br>
                                  And the other looked like a monkey, quite annoying,<br>
                                  There was another who was obsessed with his hair,<br>
                                  At Déjà Vu fell for a lord’s sis and ga ga did he go everywhere;<br>
                                  On the very next day he called her to be his date –<br>
                                  “Sorry, I’m already married” said the mother, so blame it on fate…<br>
                                </p> 


                                 <p class="text-center  text-dark text-justify text-4">The lord who was the brother of this fair maiden,<br>
                                  Didn’t like the hanky panky, so some action had to be taken;<br>
                                  So one day he took the obsessed one, and some answers did he seek,<br>
                                  Since that day cash did he pay to his bro-in-Iaw through his nose… <br>
                                </p> 


                                <p class="text-center  text-dark text-justify text-4">There were three lads in this great land,<br>
                                  One who enrolled himself in the gateway stand,<br>
                                  The other of course gave like hell to the land of German tech,<br>
                                  Well we’ll always be brothers they say, forget the sisters oh heck!<br>
                                </p> 


                                



                                <p class="text-dark text-justify text-4"><span class="font-weight-semi-bold">Articles:</span> </p>


                                <p class="text-center text-decoration-underline text-dark text-justify text-4">Que Sera Sera</p>


                                <p class="text-dark text-justify text-4">
                                </p>
                                
                                


                                <p class="text-dark text-justify text-4">“When in our death-beds, they will say; here’s a man
                                  who fought for his country’s freedom, slowly the scene in which Mel Gibson clad in his Scottish 
                                  braves kilt faded away into a familiar face, “Machang, have you seen my other slipper?” the imbecile
                                  would question me without even blinking his eye ‘Hello! Now what’s wrong with this picture. This moron
                                  has just awoken me up at two o’clock in the morn to ask me about his slipper. No! I say to myself, I’m 
                                  not going to let him have the pleasure of an answer. Therefore I turn my back on’ him and made a 
                                  desperate attempt to get back to ah yes! Mel Gibson. Well my attempts of course were in vain ‘cause all
                                  I could do was just roll from side to side. Well for one thing it was pouring out side and the tent
                                  flaps were wide open, the imbecile had forgotten to close them behind him, and I was the only one in
                                  the tent surrounded by of course, water.
                                </p>



                               <p class="text-dark text-justify text-4">Yes! I know what you’re thinking right now; obviously any sane person
                                  wouldn’t have thought twice to pack up his stuff and rampage into a dry tent. I personally think one
                                  shouldn’t abandon his post for any reason whatsoever. Well sometimes I amaze myself. Here’s a guy who
                                  had second thoughts before touching a wet coir rope, a guy would walk miles while gone camping just
                                  to get a sip of Coke. Gosh! Well I still remember how it all began. 
                               </p>




                                 <p class="text-dark text-justify text-4">It was a cool Saturday morning; clouds overshadowed the great
                                   canopy of blue as if God wanted to hide the sun’s great splendour. In brief it was just another
                                   normal day expected in the month of May. And there I was all dressed up in my Cub Scout uniform, 
                                   inside a place I always dreaded of going, while at school. At that point I never would have thought
                                   that this small house like building with a green roof would be my “home away from home”, In the years 
                                   to come. Well that day was not that interesting, for one thing everybody seemed too busy to notice a
                                   chubby guy, with a green cap with yellow lines running around it. It amazed me to see all the big
                                   “Aiyyas” in school come to and stand still on hearing a whistle call and the second it ended running
                                   around like mad bloodhounds. Finally the time came when I was introduced to a set of guys who
                                   called themselves Leopards, and learnt their strange ways of life starting with a humongous 
                                   collection of CR books bound together, which they took pride in calling their ‘Log Book’, and a 
                                   couple of artefacts they cared for a lot. And I was their new leopard, and of course I didn’t 
                                   last much long before I got my very own “call sign”, which up to this day my friends call me by.
                                </p>




                               <p class="text-dark text-justify text-4">Those years as a Leopard doing leopardy stuff, well it was more 
                                  fun than I expected it to be, but if someone asked me what the coolest thing I did as a junior, I would
                                  definitely say that it was the hike through the Attidiya wela. Gosh! Was that something or what? It was 
                                  one of my very first hikes. Well those days the Atidiya wela was one big marshy land, and once we got
                                  back to civilization we were more or less marsh hogs. Things went right that day – it rained. And all
                                  that mud washed off us so that it was easier for my parents to make me out when they came to pick me
                                  up. Well time sure files when you’re having a good time’ cause during the next couple of years I became
                                  the much important leader of the Leopards.
                                </p>



                                <p class="text-dark text-justify text-4">Being a senior was not quite bad itself. This is when the 
                                  young pruned ones are let go to the outer world of B.Y.T.’s. Naturally it was not always so, ’cause as a
                                  senior you get to learn to take a lot of slack. And digest it at the same time, ’cause believe it or not
                                  we thought the seniors do the most amount of work around this place. Out of the most things we do it is
                                  help the juniors to climb down those treacherous mountains, when insanity strikes us once in a while.
                                </p>




                                <p class="text-dark text-justify text-4">Talking about going down mountains it was when we were just 
                                  about to go down Baker’s falls it occurred to me that I was scared of heights. And then there he was a
                                  very important character talking of rubbish, as he would call himself in short our former G.S.L. 
                                  Mr. Victor, “I say! If an old man like me can go down this why can’t you!” Well those words really got
                                  through, ’cause it was somewhere in between self-proclaiming dignity and unconditional humiliation of
                                  a sixth grader. Mr. Victor passed away in ’97. I use the words ‘passed away’ as Emily Dickinson says
                                  in one her poems.’
                               </p>




                               <p class="text-dark text-justify text-4">“Unable are the loved to die, for love is immortality” By the way I
                                  did go down Baker’s fall that day. Drip! As if of a rhythmic clicking of a metronome water would find 
                                  its due course onto my face from the leak that had sprung just above my head. I would compare the beat 
                                  to one of Rachmaninoff’s pieces and contradict myself to choinnocture.
                               </p>



                               <p class="text-dark text-justify text-4">Ah yes! Seniors! Well as I was saying as seniors we have gone
                                  through a lot we’ve gone camping, been on cycle hikes and much more, which has formed a sense of
                                  brotherhood among us doing things as a team, embedding us with the slogan ‘All for one and one for
                                  all’, and of course we have occasional ‘rifts and rafts’, but then all would be forgotten after a 
                                  good session of ‘Oblix’. But I would say that the peak of our teamwork was in ’98 when we won the 
                                  C.P. Jayawardena shield after a lapse of 10 years. That sense of adrenaline gushing through your 
                                  veins when you know that alls at stake and that feeling of water running down your spine, when they 
                                  start announcing the marks, but of course it is no match to that single tear that breaks all barriers
                                  when you hear them say that the Thomians have won it, with a margin of three fourths of a point.
                               </p>



                               <p class="text-dark text-justify text-4">I’m sure this article wouldn’t be complete if I don’t
                                  mention my adventures in the land of the Sakura. Well apart from risking my neck at Bungee jumping… and
                                  yes I did jump!! The next memorable moment came when I was taken into a spa. I of course didn’t like the 
                                  place from the beginning itself and I’m sure you wouldn’t either if you were offered tea without sugar
                                  with dry fish. Nevertheless after pouring the tea down’ my throat, I headed through to the changing 
                                  room with much enthusiasm ’cause it was explained to me that this was one of the customs in their land.
                                  Well the customs sure puzzled me ’cause soon as I came in I was handed two towels. One bath towel and
                                  one the size of a face towel! Now the bath towel I could rectify but the small one-what on earth 
                                  for? Well all my queries were answered the pool area to notice dozens of natives people walking 
                                  around with just the face towel, the only difference being that it was not on their faces. I’m sure
                                  that you have thousands of thoughts in your mind right now and one of it is about whether I went
                                  through it or not. Let me just say that it’s for me to know and you to find out… if possible that 
                                  is.
                               </p>



                               <p class="text-dark text-justify text-4">Throughout these years Scouting developed into an obsession,
                                  it has taken me places of intangible beauty, made me come across people of different calibres from the
                                  wise to the beauteous, from intellectuals to the differently abled. It has made me climb unexplored
                                  mountains just to glimpse the splendour of a dying sun. Made me realize the gratitude in a lad’s eyes,
                                  every time I help him climb a tricky part, to let a first time camper clutch onto to my shirt as I 
                                  take him to his tent in darkness and won’t mention it. After going through all this when I was made 
                                  T/L the one thought that came to mind was who would have thought that the once chubby guy who came 
                                  seven years back would come to this. When that year came to an end the one thing that I found out most
                                  was that we will probably never know or understand the impact we have on other people’s lives. We
                                  are all given that opportunity sometimes or another, to affect people’s lives for the better or for 
                                  the worse.
                               </p>



                               <p class="text-dark text-justify text-4">That morning I was woken by a lad who had come with my tea.
                                  I would thank him for his gesture. Then as the French would say ‘coup de fourde’; struck by lightning. 
                                  That sense of déjà ‘vu that swept through my mind was surely meant to be put in print.
                               </p>




                               <p class="text-dark text-justify text-4">After reading this article if you feel the same way, having had
                                  the privilege of going through the portals of S. Thomas’ I salute you sir! If not don’t feel so cause as
                                  the saying goes “What will be, will be.”
                               </p>



                              



