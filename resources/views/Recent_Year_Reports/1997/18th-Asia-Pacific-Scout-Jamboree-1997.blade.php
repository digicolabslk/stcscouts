                              <p class="text-dark text-justify text-4">Green Cord Junior Scout M.B. Perera and Efficiency Decora­tion Senior Scout
                                 A. Farook were among the 25 members of the contingent selected to represent Sri Lanka at the 18<sup>th</sup> Asia
                                  Pacific & 9<sup>th</sup> Malaysian National Jamboree in Terengganu, Malaysia. The Chief Commissioner Mr. Camillus
                                   Fernando presented the Sri Lankan flag to our contingent leader Mr. M.F.S. Muheed, to be hoisted at
                                    the Jamboree.
                              </p>


                              <p class="text-dark text-justify text-4">The 18<sup>th</sup> Asia Pacific Jamboree was held at Sri Keluang Youth Camp, Besut,
                                 Terengganu, Malaysia from 1<sup>st</sup> to the 8<sup>th</sup> of August. About 20,000 Scouts and Girl Scouts and Guides
                                  from 23 countries from the region and Europe took part in this Jamboree.
                              </p>


                              <p class="text-dark text-justify text-4">The theme “Towards Developing an Excellent Youth” was cho­sen as it best reflects the 
                                fundamental Scouting ideals and strategy for Scouting towards 2000.
                              </p>


                              <p class="text-dark text-justify text-4">On the 1<sup>st</sup>, early morning we all gathered at National H.Q. and went in a coach to the 
                                Airport. Then we took a direct 3 hour flight to Kuala Lumpur Airport. We were welcomed by 2 Malaysian
                                 Scout Leaders. Then we took another 12 hour journey by coach to the jamboree site in Terengganu which 
                                 is in East Malaysia. It was 3 a.m. by the time we arrived at the campsite. So we slept in the 
                                 Inter­national Secretariat till morning. We were given a welcome break­fast of pizza, bread & salad. 
                                 Then we were taken to the campsite by a special bus and the Malaysian Army brought our tents and camp
                                  equipment. Then we arranged our site. The same day, the Opening Ceremony of the jamboree was held.
                                   It was a very grand event.
                              </p>



                              <p class="text-dark text-justify text-4">Next day we were given activity tickets to take part in jambo­ree
                                 activities, both indoor and outdoor, which included obstacle circuits, pioneering projects, tracking,
                                  off-road cycling, wall climbing, orienteering, computing skills, firemanship, skate boarding, beach 
                                  games, swimming. canoeing, hiking and many activities which were on for the rest of the Jamboree.

                               </p>




                              <p class="text-dark text-justify text-4">  Akram Farook was the quarter master of food. Bimantha Perera
                                 was selected to represent Sri Lanka at the 18<sup>th</sup> Asia Pacific Regional / 9<sup>th</sup> Malaysian National Jamboree 
                                 Youth Forum. He was elected as the Vice Chairman of the Recommendation Committee. The Youth Forum mainly
                                  focused on the theme “Towards Developing Excellent Youth” for discussions. Its aim was to reflect 
                                  awareness that knowledgeable thinkers have a better chance of taking charge of their lives and achieving
                                   personal advancement and fulfillment; and to meet the challenges of living in a technologically 
                                   ori­ented, multi-cultural world towards achieving Scouting Vision 2000.
                              </p>



                              <p class="text-dark text-justify text-4">During the jamboree we went for the cultural show and inter­national night. The Devil
                                 Dance for the cultural show was our item where we managed to share a part of our culture with them.
                                  During our Sri Lanka Day we were all dressed up in our National Dress. We displayed a Sri Lanka corner
                                   with arts and crafts. We served the other contingents with Sri Lankan sweets and we sang with them.
                                    “Suranganita Malu Genawa” was a hit with everyone.
                              </p>



                              <p class="text-dark text-justify text-4">During the jamboree we were also invited by many contingents for their
                                 cultural days, campfires, meals or to make friends. We also explored the jamboree site which was a 
                                 very large area of 288 acres. We exchanged many badges and souvenirs and made friends with many other
                                  scouts. The grand closing ceremony included a musical show which got everybody dancing and singing.
                              </p>




                              <p class="text-dark text-justify text-4">On the 9<sup>th</sup> morning everything was packed and ready. After breakfast
                                 we said good bye and got into the coach and came back to Kuala Lumpur after a 12 hour journey.
                                  In Kuala Lumpur, we stayed at Kuala Lumpur District headquarters. The next 4 days we toured and 
                                  did shopping in Kuala Lumpur.
                              </p>


                              <p class="text-dark text-justify text-4">On the 13<sup>th</sup> of August we look a flight to Colombo via Singapore leaving us just
                                 a few days for the Colombo Camporee.
                              </p>


