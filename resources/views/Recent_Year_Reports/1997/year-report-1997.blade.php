                             <p class="text-dark text-justify text-4 text-start">
                             <span class="font-weight-bold" style="text-decoration: underline; color: black;">1<sup>st</sup> Term:
                             </p>



                              <p class="text-dark text-justify text-4">The first term of the year 1997 was most certainly a 
                                very busy one for the Scouts of 16<sup>th</sup> Colombo.
                              </p>


                              <p class="text-dark text-justify text-4">On the 22<sup>nd</sup> of February the Scouts participated in a rally
                                 to mark the birthday of our founder Lord Baden Powell. On that same day we were also invited for
                                  two Peace Walks and Thinking Day rallies organised by the Guides.
                              </p>


                              <p class="text-dark text-justify text-4">This year at the Job Week the Scouts did well in collecting a record 
                                figure of over 100,000/= rupees during the period of 10 days in which it was held.
                              </p>


                              <p class="text-dark text-justify text-4">Our Scouts did exceptionally well at the 15<sup>th</sup> Annual Scout Swimming 
                                and Diving Championships held at the Josephian pool. They emerged Champions in all the age groups
                                 except one in which they were placed runners up. But the height of the event was the Thomians winning
                                  the Relay Championship. Diving champs and the S. Aloysious Memorial Challenge Shield awarded to the 
                                  overall champion for the 6th consecutive year. This year the team was led by Suresh Peter.
                              </p>



                              <p class="text-dark text-justify text-4">At the Presidential Scout Awards Ceremony held on the 13<sup>th</sup> of March 1997,
                                 Mr. Daniel Edirisinghe was awarded the Silver Lotus for an especially distinguished service,
                                 and Mr. A.L. Victor and Mr. M. Nanda R. Fernando were awarded the Certificate of Merit for a 
                                 faithful service by the Chief Scout Sri Lanka H.E. the President Chandrika Bandaranayaka Kumaranatunge.
                              </p>




                              <p class="text-dark text-justify text-4">Five of our senior Scouts did well in passing their final interview for the
                                 President’s Award, which is the highest award a Scout can win. They are Rakitha Wanigatunge, 
                                 Isuru Suriyapperuma, Pasan Perera, Dananjaya Peter and Milindu Mallawaratchie. We congratulate them
                                  on their achievement.
                              </p>

                              <p class="text-dark text-justify text-4">During this term the Scouts were invited to campfires organised by the Prep. 
                                School and Royal College. During the course or this term, two projects in order to help the community
                                 were organized at the Cheshire Home for the handicapped and at Gamini Martha Elders’ Home. The Thomian
                                  Scouts also helped out at the Sports Meet of the School for the Blind, Ratmalana.
                              </p>

                              <p class="text-dark text-justify text-4">The Thomian Scouts also helped out the Girl Guides of Colombo at their
                                 “April Rhythms ‘97” fun fair held during the holidays. This was the first event at which the 
                                 “Dreaded Dunk” was born, a brainchild of the Thomian Scouts.
                              </p>

                              <p class="text-dark text-justify text-4 text-start">
                             <span class="font-weight-bold" style="text-decoration: underline; color: black;">2<sup>nd</sup> Term:
                             </p>



                              <p class="text-dark text-justify text-4">Even though this term was as successful as the previous ones the
                                 Scout Troop suffered a great loss as Mr. A.L. Victor our Group Scout Leader passed away on
                                  the 11<sup>th</sup> of June after serv­ing the troop for many years. He was a very dedicated Scoutmaster who
                                   stood by us through thick and thin and worked for the betterment of the Troop.
                              </p>



                              <p class="text-dark text-justify text-4">During the course of this term we took a pioneering step forward in 
                                organising “Cook-It-Out ‘97” a cooking competition, inviting Girl Guides of 23<sup>rd</sup> Colombo and
                                 11<sup>th</sup> Colombo Guide Companies (Museaus and Visakha). This was followed by “Troop Night” – a one
                                  night camp in College for the Scouts, who enjoyed it thoroughly.
                              </p>




                              <p class="text-dark text-justify text-4">We were invited by the Scouts of Royal College for their Scout Day
                                 and we also staged a drama at the evening of entertainment organised by the Guide Company of
                                  Girls’ High School.
                              </p>


                              <p class="text-dark text-justify text-4">Our scouts participated in several Community Development Projects
                                 which included the “Clean the City Campaign” organised by the Colombo Municipal Council and several
                                  other projects at the Cheshire Home, Dehiwala and Helpage Elders’ Daycare centre, Ratmalana.
                              </p>



                             

                              <p class="text-dark text-justify text-4 text-start">
                             <span class="font-weight-bold" style="text-decoration: underline; color: black;">3<sup>rd</sup> Term:
                             </p>


                             <p class="text-dark text-justify text-4">The third term was, as always a very active and enjoyable one for the scouts.
                              </p>


                              <p class="text-dark text-justify text-4">At the 36<sup>th</sup> Annual Scout Camporee, the scouts did exceptionally well in 
                                winning pennants on both days or inspection and col­lecting the highest points in the sub-camp
                                 for maintaining high standards in camping.
                              </p>


                              <p class="text-dark text-justify text-4">During this Camporee, five of our Senior Scouts were awarded the President’s Award badges.
                                 They are I.U.P. Suriyapperuma, D.N. Peter, K.P.L. Perera, M. Mallawaratchie and R. Wanigathunga.
                              </p>


                              <p class="text-dark text-justify text-4">Three more senior scouts passed the final Interviews for the President’s Award within the
                                 course of this term. They are N.D. Peiris, S.N. Peter and A.A. Farook.
                              </p>


                              <p class="text-dark text-justify text-4">Sri Lanka was represented by two of our scouts, A.A. Farook and M.B. Perera, at the 
                                18<sup>th</sup> Asia Pacific Jamboree held in Malay­sia. They brought with them more experience and wider knowledge
                                 to enrich our Troop
                              </p>

                              <p class="text-dark text-justify text-4">Having been invited, our scouts participated in the campfire organised by the Scouts of
                                 Royal College and the “Scout Day” by the scouts of Ananda College.
                              </p>

                              <p class="text-dark text-justify text-4">Our Scouts participated in the first ever Jamboree on the Internet (JOTI) which was held 
                                on the 18<sup>th</sup> of October. This was the first time a jamboree of this nature was ever held. Our scouts
                                 communi­cated with Scouts and Guides of other countries through the Internet. This was conducted with
                                  the assistance of one of our A.S.L.s B.R. Kulapala.
                              </p>


                              <p class="text-dark text-justify text-4">This year the 16<sup>th</sup> Colombo Scout Troop celebrates 75 years of Thomian scouting. As one of
                                 the main activities in connection with this, the scouts organised “Coastal Winds”, a Scouts and
                                  Guides get together for two days during which a concert was staged. The Chief Commissioner of
                                   Scouts, Mr. Camillus Fernando was the Chief Guest at this event which was successfully concluded.
                              </p>


                              <p class="text-dark text-justify text-4">Finally we would like to thank Mr. Daniel Edirisinghe, Mr. A.L. Victor and all Asst. Scout Leaders for their 
                                advice and guidance to the troop throughout the term to maintain high standards of the Troop.
                              </p>


                

                           
                            
