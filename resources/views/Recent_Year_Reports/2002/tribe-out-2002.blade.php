                               <p class="text-dark text-justify text-4 text-center text-decoration-underline">
                              <span class="font-weight-semi-bold">Jamboree Story: Jayanga</span>
                              </p>

                              <p class="text-dark text-justify text-4">it finally came, the month
                                 of August. The month so many people were waiting for… (For what?) I’ll tell ya, it was the 6<sup>th</sup>  National 
                                 Jamboree. For some of us “Jamboree” was a real uneasy feeling (Mostly for the people who had been to 
                                 the previous one). But for others, especially for the juniors it was something really worth looking
                                  forward to. We left on route to Balapitiya by bus. It was quite a joyful ride especially when Thunge
                                   was there (Thunge is a unique Megabyte character who holds the record for the most number of items 
                                   possessed, from his shopping spree after every camp). Nevertheless he doesn’t miss any camp 
                                   (We know why). After having got to Balapitiya, we were wondering whether we were at the right place 
                                   (Guess!!). At the small area which was allocated by the officials we got ourselves organized and 
                                   started working on our gadgets. I was in charge of the Kitchen crew & A.D. was in charge of the Tower
                                    crew with Udda & Ravindu. Now Building a kitchen at any cost wasn’t an easy job. I had Bimal & 
                                    Dhineshka in my crew. It really wasn’t an enjoyable experience working out getting BBQ’d under the hot 
                                    sun (Maru Katta). But towards the evening things got pretty cool & comfy. I had a real tough (but fun)
                                    time with the two guys quarrelling & arguing Ajiiiiiith Baaaaaaaaa. But anyway by around 3 a.m. the 
                                    next day after a lot of hard work and commitment the construction job of both the tower & kitchen was
                                    over (Fine with that).</p>


                               <p class="text-dark text-justify text-4">The next day things were a lot easier than the first day. After
                                 putting the final touches to the gadgets we went to the showers for a bath. Funny to say but the 
                                 shower’s unusual behaviour (The water stops pouring in the middle of a bath) was a bit of a problem 
                                 to everyone. We overcame our problem when our official P.R.O. (guess who!!) became friendly with a 
                                 very helpful “pani akka” in the neighbourhood who let us use their well. At last that problem was
                                  solved. Among our other problems was the lack of H2O for the K.Y.B.O. For this we had to even run 
                                  back to the campsite from the KYBOs to take drinking water in extreme emergencies (Talking with 
                                  experience).
                              </p>


                              <p class="text-dark text-justify text-4">Day three was practically an ice day except for the Inspections.
                                 Our troop won a pennant for achieving a very high standard at the inspection. Singing & taking each 
                                 other for a byte was our favourite past time & it was also the guides’ day, so we had quite a number
                                  of visitors around (Sachi had a great time for sure). As usual our P.R.O. was in business entertaining
                                   the visitors with his famous jokes. Among the prominent personalities at the Jamboree were Janith,
                                    popularly known as “Happy Cow” or Moooo. This chap, who was the deputy in charge of gear, plugged 
                                    into the kitchen (leaving aside all his duties) and started cooking with his best friend Zafar (Apit),
                                     who was the Asst. Quartermaster food (The only problem was that they virtually kept doing all the
                                      unnecessary things in the kitchen & ultimately got scolded by the G.S.L.).
                              </p>

                              <p class="text-dark text-justify text-4">But fortunately we had our Great Cook or more or less the Quartermaster
                                 Food, Daham (Sindu Bindu) in the kitchen. Because of him we were able to eat good food on all the days 
                                 (So he thinks). The camp doctor D.I. (ping pong) was practically invisible during night due to his dark
                                  complexion. But fortunately for us, he had his teeth well brushed, so we could recognize him without
                                   much difficulty even during the night. This chap had a very nice time treating S*n*k* even though he
                                    never had any illness. (maattu). Janith as usual did all sorts of feats to prove others that he was
                                  called mooo for good reasons. (E.g.: Poured hot coriander in to Ravindu’s ear to test his sensors).
                                   Now that’s nothing new to us coz we know Mooo very well. Now coming to Indika, our fair but short
                                    C.I.S. Man was behind the scene all the time. Favours chatting with the instructors and uses all
                                  sorts of bara kadda words (We need dictionaries to figure out what he’s saying). Now did I miss
                                  anyone – of course the most important person at the camp (Naturally the camp warden) Sachi. 
                                  Talking about Sachi, he did a maxxa job icing (just kidding Sachi). To tell the truth he had
                                   everything under control, (except for a few cases naturally) showed off the colours of a good 
                                   leader & set an example to others as a real worthy Scout (He bribed me into saying these).
                              </p>


                               <p class="text-dark text-justify text-4">Day four started off with the usual inspection & we ended
                                 up earning another pennant for our high standards. There were many outdoor activities during the
                                  day time. But we (The Seniors) were pretty happy to stay at the campsite without going out for
                                   activities mainly because we had more fun remaining at the campsite. One such was the Oblix time.
                                    (Wander what?) Ahhhhh Mooo……..
                              </p>


                              
                               <p class="text-dark text-justify text-4">Day five was a little special than the other days coz the
                                 organizer took us out to various places outside the Jamboree premises. We got the chance to go out on
                                  a boat ride in Unawatuna which was included in one of the outings. We were able to get back to the 
                                  campsite without getting wet despite of the heavy rain we encountered during the tour to Unawatuna. As
                                   usual our singing and shouting started at night living up the campsite where else other troops were 
                                   fast asleep.
                              </p>

                              
                               <p class="text-dark text-justify text-4">Day six was mostly spent sleeping under the tents from morning
                                 till evening & eating grub. Autographing the souvenirs & taking photographs were held at the final 
                                 official day of the Jamboree. The most enjoyable time for us however was the last night. The majestic 
                                 Senior Special dinner was made by us (duhh…). It was a grand feast & everybody enjoyed the rich food, 
                                 (comparing with other days it was superb). We made the juniors sleep early and went on with our plans
                                  for the night (You know what I mean??). Some went on the souvenir hunt while Ravindu and I made the
                                   “Thunge Special” which included all the left overs from dinner) Uhhhhh Yuk! Janith did the proceedings
                                    and soon there was a loud roar of laughter from all of us. In the middle of the excitement Bimal fell
                                  on our Scout Leader’s tent and it completely fell onto the ground waking up the S.L with a jump 
                                 (That gave him a shock all right hehe). Ahem, enough of that – now it was the juniors’ turn. Toothpaste,
                                 Salon pas, Siddhalepa and Jam were what they got for their treat. That did the trick all right. It
                                 sure made us remember the past days, when we were juniors getting the same kind of treatment
                                 from the seniors. Face it Juniors – that’s life Hahaha.
                              </p>


                              
                               <p class="text-dark text-justify text-4">Seven wonderful days went by, it was very sad for all of us to
                                 think that we had come to the end of such a wonderful period of time filled with laughter & excitement.
                                  It was always such a great feeling to be in camp in brotherhood as one family. Even though we had to
                                   part we knew that Scouting will keep us as friends & brothers together forever. All the fun & the 
                                   joyful things that happened in the 6th National Jamboree at Balapitiya will remain in our hearts
                                    for many years to come.
                              </p>



                              
                               <p class="text-dark text-justify text-4">“Scouting Is Life” The rest is just details…
                              </p>

                              <p class="text-dark text-justify text-4 text-end">
                              <span class="font-weight-semi-bold">Jayanga</span>
                               </p>


