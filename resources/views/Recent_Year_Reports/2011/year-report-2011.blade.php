                            <p class="text-dark text-justify text-4">The Annual Colombo District Camporee was held from
                                the 1<sup>st</sup> to 5<sup>th</sup> September 2011, at the Vihara Maha Devi Park coinciding
                                with the Thomian
                                Fair. With sacrifice and determination the Troop managed to put up 2 dunk stalls at the
                                fair and perform exceptionally well in maintaining standards and were awarded with 2
                                pennants and the title “Best Kitchen” for maintaining high camping standards. This was
                                certainly a test of the toughness of the Scout Troop, as been in both places was certainly
                                a challenge, and took a lot of the boys, but we did it, and cherish every moment of the
                                experience.
                            </p>


