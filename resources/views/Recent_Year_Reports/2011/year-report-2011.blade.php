                            <p class="text-dark text-justify text-4">The 16<sup>th</sup> Colombo was given the opportunity
                                to
                                host the Annual Colombo District Swimming meet for the year 2011, which was held on
                                the 15<sup>th</sup> of October, at the College swimming pool. A team comprising of 25 scouts
                                took part in the meet and the troop emerged as the “Trophy Category – Runners Up” at this
                                event.
                            </p>


                            <p class="text-dark text-justify text-4">The troop initialized a Planting Project with the
                                objective of promoting environment conservation. The project is manoeuvred by planting
                                foliage in the back garden and on one of the side gardens of the scout room. This
                                project was initiated in relation to the ‘Go Green’ project that is currently
                                been carried out in College.
                            </p>


                            <p class="text-dark text-justify text-4">The Colombo District Patrol Leaders’ Training
                                camp was held on the 21<sup>st</sup> and 22<sup>nd</sup> of October 2011 for which 2 of our
                                junior scouts
                                participated. Scout A.A. Cader was selected to represent the Sri Lanka Scout Association
                                at the Asia Pacific Youth Conference in Japan from the 29<sup>th</sup> October –
                                6<sup>th</sup> of November 2011.
                                The Annual Troop Night was held on the 9<sup>th</sup> -10<sup>th</sup> of December 2011.
                                This brought together
                                past and present Scouts of all time and was time to immense fun.
                            </p>


                            <p class="text-dark text-justify text-4">Scouts C.D. De Mel, A.A. Cader, S.C. Gajanayaka, N.J.
                                Wikkremathileke
                                and D.G.D. Colonne conducted community service projects in order to help those in need as a
                                requirement in the President’s Scout Award and they have achieved their President’s Scout
                                Awards.
                                The President’s Award is the highest award a scout could achieve while in school.
                            </p>


                            <p class="text-dark text-justify text-4">The troop is furthermore in the process of planning a
                                unique campfire titled “Centennial Flames 2012” to be held in February 2012 to celebrate a
                                centenary of selfless service of Scouting in Sri Lanka and in our College. An event of this
                                nature is being held in College after 2004, when the troop last organized “Tribal Craft” – a
                                unique Scout Exhibition.
                            </p>


                            <p class="text-dark text-justify text-4">Although the year may conclude; the lessons learnt,
                                the bonds built, the character established and the memories gained and the experiences would
                                never leave the hearts of the scouts. Our story would not be complete without acknowledging
                                those who made all of the above possible. We take this opportunity to thank the
