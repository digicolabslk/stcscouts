                            <p class="text-dark text-justify text-4">With the vision of creating a better Scout Troop,
                                not simply in winning awards, but seen through a positive change in the character of every
                                one of the 60 scouts. A troop of scouts gathered together to make this vision a reality,
                                they
                                gathered as one family to face every challenge that came their way and successfully triumph
                                over
                                them. Mentioned below is how they did it, this is our story.
                            </p>


                            <p class="text-dark text-justify text-4">The first program was the recruitment of scouts. The
                                existing
                                members encouraged other students to join the scout movement, by inviting them to be a part
                                of many
                                interesting programs organized; recruitment also took place through the going up ceremony
                                held on
                                the 11<sup>th</sup> of February 2011.
                            </p>


                            <p class="text-dark text-justify text-4">In order to build a solid junior troop a Patrol
                                Leaders’ Training
                                Camp was held on the 5<sup>th</sup> & 6<sup>th</sup> of February 2011 at the Scout Room. The
                                prime objective behind the training
                                camp was to enhance the leadership skills of the senior most members, who function as the
                                leaders of
                                the junior troop. This was achieved through the many interesting sessions and leadership
                                games
                                that were carried out. A total of 20 scouts participated in the mentioned event.
                            </p>


                            <p class="text-dark text-justify text-4">With the intention of developing the skills of the
                                senior troop,
                                a Seniors’ Training Camp was also held in the same month. The sessions and training programs
                                were
                                all conducted by the Instructors of the 16<sup>th</sup> Colombo Scout Troop.
                            </p>


                            <p class="text-dark text-justify text-4">The President’s Scout Evaluation Camp was held from
                                the 28<sup>th</sup> – 30<sup>th</sup> of January 2011 at the Salawa campsite in Awissawella,
                                five scouts took part and
                                successfully completed the evaluation program.
                            </p>




                            <p class="text-dark text-justify text-4">A special project of selling car stickers was carried
                                out coinciding with the
                                Old Boys’ week during the month of February. The project was a success as the troop was able
                                to collect
                                a considerable sum of money on this day.
                            </p>



                            <p class="text-dark text-justify text-4">In the month of February, the district organized a
                                special occasion to
                                celebrate B.P.’s Day. Eight of our scouts attended the ceremony, which commenced with a
                                service held at St. Andrew’s
                                Scotskirk Church, Kollupitiya. This was followed by the continuation of a cycle parade
                                commemorating the same, which
                                had commenced from Jaffna and concluded at Moratuwa.
                            </p>



                            <p class="text-dark text-justify text-4">The Colombo District Sports Gala for Scouts and Cub
                                Scouts organized
                                by the Colombo District Scout Association was held on 14<sup>th</sup> May 2011 at Hendry
                                Pedris Grounds. 16<sup>th</sup> Colombo
                                emerged as runners-up of the competition, the Scouts performed exceptionally well at their
                                events,
                                winning most of the events including individual events, relays and the tug of war.
                            </p>




                            <p class="text-dark text-justify text-4">With the objective of strengthening the bond between
                                scouts and guides,
                                a special program titled “Meltdown 2011” was organized on the 21<sup>st</sup> of May 2011.
                                The program brought
                                both the scouts of S.T.C. and many active Guide Companies in the Colombo District under one
                                roof to experience many interesting games, a unique treasure hunt and a cooking competition
                                which was the highlight of the day. This event was a huge success with a turnout of over 50
                                participants.
                            </p>


                            <p class="text-dark text-justify text-4">With 3 weeks to spare for exam preparation, the
                                16<sup>th</sup> Colombo scouts conducted their annual Service and Relation Week from the
                                28<sup>th</sup> of May
                                to the 7<sup>th</sup> of June 2011. 16<sup>th</sup> Colombo emerged as the “Highest
                                Collectors” of the island.
                                The scouts maintained high levels of discipline and carried the respective tasks to the
                                best of their abilities throughout the week.
                            </p>



                            <p class="text-dark text-justify text-4">The prestigious C.P. Jayawardene Challenge Shield and
                                Mayor’s Flag
                                Competition was held on 16<sup>th</sup> July 2011 at St. Joseph’s College. With intense
                                training for over
                                a month the 16<sup>th</sup> Colombo Scout Troop yet again was able to make history by
                                winning both
                                competitions for the 3<sup>rd</sup> consecutive year. The C.P. Jayawardene Challenge Shield
                                was secured
                                by the Troop for the 3<sup>rd</sup> consecutive year, while the Mayor’s Flag was retained
                                for the 6<sup>th</sup>
                                consecutive year. The Mayor’s Flag team comprised J.J. Kamalanujan (Patrol Leader), P.
                                Jathurshan
                                (Asst. Patrol Leader), G. Hamshavathanan, D. Palapathwala, N.P. Edirisinghe, D.S.
                                Samaraweera,
                                M.D.S. Fernando and V.D. Liyanagamage. The C.P.J. team comprised S.C. Gajanayaka (Patrol
                                Leader),
                                M.F. Faizal (Asst. Patrol Leader), A.A. Cader, D.G.D. Colonne, S.R. Faber, N.J.
                                Wickramatilleke,
                                K.A. Sudesh Sanjaya and N.J. Bulathsinghala.



                            <p class="text-dark text-justify text-4">Four Thomian Scouts namely S.C. Gajanayaka, N.J.
                                Bulathsinghala,
                                D.G.D Colonne and T.S Rajan were selected to represent Sri Lanka at the 22<sup>nd</sup>
                                World Scout
                                Jamboree which was held in Sweden from the 27th of July to the 07th of August 2011.
                            </p>


