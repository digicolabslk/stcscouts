                                Mr. M.R.M. Hamza, Past Scouts, Parents of the Scouts and all Senior and Junior
                                Scouts of the troop for their untiring efforts and contribution towards making
                                the year 2011 a marveling success.
                            </p>



                            <p class="text-dark text-justify text-4">As a year comes to an end and another year dawns,
                                the legacies left, have yet again proved that the Thomian Scouts stand for none other than
                                uniqueness, skill and perfection



