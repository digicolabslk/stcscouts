                             <p class="text-dark text-justify text-4 text-start">
                             <span class="font-weight-bold" style="text-decoration: underline; color: black;">1<sup>st</sup> Term:</span>
                             </p>
                             

                             


                              <p class="text-dark text-justify text-4">The year commenced with the Troop immediately
                                 given the task of organizing the Anglican Christian service to commemorate the centenary year as
                                  part of a religious ceremony. Held at the Cathedral of Christ the Living Savior on the 18<sup>th</sup> of 
                                  January, this service was officiated by Rev. Marc Billimoria and Rev. Amal Fernando as well as by
                                   the Arch Deacon of Colombo, Rev. Perry Brohier. This event turned out a high attendance as the Scout
                                    Troop was successfully able to organize this service.
                              </p>


                              <p class="text-dark text-justify text-4">The next event the Troop had to look forward to was the Thomian Triathlon. 
                                Held on the 25<sup>th</sup> of January, this event was considered to be important in the school calendar and once
                                 again the Scouts were able to display their skills when they were asked to put up the Gateway at the 
                                 Main Gate of the school. The Scouts also were able to put up the famous ‘Scout Dunk’, receiving a large 
                                 crowd at this event for being a major attraction. Following this was the Centenary Walk held on
                                  the 22<sup>nd</sup> of February that commenced from Galle Face and finished at the Vihara Mahadevi Park. 
                                  The Scout Troop participated by decorating a truck and organizing a double-decker bus, easily 
                                  becoming the most prominent Troop at this event.
                              </p>


                              <p class="text-dark text-justify text-4">The event that brought this term to a conclusion was the Annual Holiday
                                 Camp. Returning to a campsite named ‘Pedro’ deep in the heart of Nuwara Eliya after a lapse of 7 years,
                                  from the 23<sup>rd</sup> to the 27<sup>th</sup> of April, brought back nostalgic memories to the Scouts who had visited this
                                   place before. This camp turned out to be a tremendous success for it was able to provide many memories
                                    and moments that will never be matched while also presenting a once in a lifetime experience as well
                                     to a few.


                              <p class="text-dark text-justify text-4 text-start">
                             <span class="font-weight-bold" style="text-decoration: underline; color: black;">2<sup>nd</sup> Term: </span>
                             </p>
                             


                              <p class="text-dark text-justify text-4">The second term began with the District Scouts Sports Gala which the Scout Troop
                                 participated in, ending this event being judged 2<sup>nd</sup> runners up. Despite their position, the Troop
                                  was able to learn a lot, proving their values can be upheld even on the sports field. Immediately 
                                  following this event was the Scout Service and Relationship Week. After two weeks of continuous
                                   toil and hard work, the Troop once again emerged the ‘Highest Collectors of the island’ – an 
                                   achievement that they have continued to receive for the past few years.
                              </p>



                              <p class="text-dark text-justify text-4">Possibly the most important as well as the final event of this term was 
                                the Colombo Centenary Scout Jamboree. Being a camp that spanned for seven days from the 1<sup>st</sup> to the 7<sup>th</sup> of
                                 August, it was held on a grand scale in Madampe. The Scout Troop was able to build a prominent tower and
                                  entrance, earning credit for their skills. They displayed high camping standards yet again and was 
                                  awarded a pennant for this. The District Commissioners Flag which is awarded to the Best Scout Group
                                   of the District was won by the Troop for the 4<sup>th</sup> consecutive year and was awarded at this event.
                              </p>


                              <p class="text-dark text-justify text-4 text-start">
                             <span class="font-weight-bold" style="text-decoration: underline; color: black;">3<sup>rd</sup> Term: </span>
                             </p>
                             <br>



                              <p class="text-dark text-justify text-4">Rarely taking place in the latter part of the year, the term began with
                                 two important District competitions which the Troop has been triumphing in for the past few years.
                                  In the competition which is to judge the best Junior Scout Troop of the District, the Troop was 
                                  placed runners up this year, losing by a slight margin. However, in the Brig. C.P. Jayawardena
                                   Challenge Shield competition to judge the best Senior Scout Troop, the Troop was able to emerge 
                                   victorious retaining the shield for the 6<sup>th</sup> consecutive year by a record margin of points.
                              </p>



                              <p class="text-dark text-justify text-4">As done every year, the Troop decided to give something back to the
                                 community by successfully organizing the Troop Community Service Project on the 25<sup>th</sup> of October at
                                  the ‘Sri Jinananda’ Children’s Development Centre. After painting and refurbishing the centre, the
                                   Scouts entertained the children by singing songs. The objective of this project was to create 
                                   awareness among the Scouts, of the unfortunate living in our society and to provide them with happiness.
                              </p>




                              <p class="text-dark text-justify text-4">The final District competition for the year was the Scout Swimming Meet
                                 held at the Wesley College swimming pool on the 8<sup>th</sup> of November. Sending a select few to participate
                                  in the event, the Scouts were able to win numerous races amidst tough competition, finally clinching
                                   the Ranil Abeynayake Memorial Trophy as the champions after a lapse of a year.
                              </p>


                              <p class="text-dark text-justify text-4"> The Scout Group continued to make groundbreaking achievements when
                                 they became the first school in Sri Lanka to introduce ‘Singithi Scouting’. At the inaugural 
                                 Investiture Ceremony, the Scouts provided a Guard of Honor for the chief guest, the First Lady,
                                  Madam Shiranthi Rajapaksha.
                              </p>



                              <p class="text-dark text-justify text-4">The final event in the Troop calendar was the Patrol
                                 camp held from the 8<sup>th</sup> to the 11<sup>th</sup> of December at Kaduwela. A lot of effort and hard work was put
                                  in by the Patrol Leaders towards the success of this camp and the hard work paid off, for the camp
                                   left a lasting impression on everyone who attended it.
                              </p>



                              <p class="text-dark text-justify text-4">Our most sincere thanks and gratitude goes out to the Warden, Sub Warden,
                                 Group Scout Leader Mrs. Wathsala Wijewickrama, Scout Leader Mr. Suranga Delpagoda, the Group Committee 
                                 led by Mr. Senaka de Fonseka, the Director of Sports, all the Instructors of the Troop, parents of
                                  Scouts and finally the Scouts for their continuous support, dedication and participation towards
                                   the success of the Scout Troop of College.
                              </p>

                               <p class="text-dark text-justify text-4">As the year came to an end, all the Scouts were reminded of how 
                                much they can achieve with their capabilities and skills, setting an example for the other Troops 
                                to follow and ending yet another year with memories, moments, and lessons that will last a lifetime.
                              </p>


                            
