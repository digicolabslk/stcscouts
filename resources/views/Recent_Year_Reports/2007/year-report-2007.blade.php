                                    <p class="text-dark text-justify text-4">The dawn of the year 2007 marked the centenary anniversary of scouting worldwide. 
                                       Scouting was founded in 1907, in England by Sir Robert Baden Powell, a Colonel in 
                                       the British Military, and has been declared the largest youth organisation in the
                                        world by United Nations, with over 28 million Scouts across the globe. </p>
                             
                                       
                                        
                                     <p class="text-dark text-justify text-4">A special walk was organised by the Colombo District
                                     Scout Association and was held on the 24<sup>th</sup> of February to commemorate Founder’s Day. A group 
                                     of Scouts from 16th Colombo participated in this event.
                                    </p>   
                               
                              
                                     <p class="text-dark text-justify text-4">The Scout Troop held its annual holiday camp at
                                 S. Thomas’ College, Gurutalawa from the 24<sup>th</sup> to the 29<sup>th</sup> of April 2007.  A total of forty-five 
                                 Scouts participated in this camp, which was both enjoyable and educational.
                                  </p>  
                                  
                                  
                                  <p class="text-dark text-justify text-4">The Troop also participated in the annual Scouts’ Sports Meet
                                     organised by the Colombo District Patrol Leaders’ Council Forum held on the 9<sup>th</sup> of May 2007.
                                      The outstanding athletic performances of the Thomian Scouts helped them clinch many medals at the event.
                                  </p> 




                                  <p class="text-dark text-justify text-4">On the 16<sup>th</sup> of May 2007, the College Scouts organised the annual
                                     “Cook-it-Out” competition. The guide companies of Ladies’ College, Visaka Vidyalaya and Musaeus College 
                                     participated at the above event.
                                  </p> 



                                  <p class="text-dark text-justify text-4">The entire Troop actively participated in the Annual Colombo District
                                     Public Service and Relationship Week (held from the 17<sup>th</sup> to the 23<sup>rd</sup> of June), and earned the highest collection 
                                     in the district.




                                  <p class="text-dark text-justify text-4">The Scouts also emerged winners of the prestigious Mayor’s Flag competition for 
                                    the Best Junior Scout Troop in the District, for the second consecutive year, and became Runners-up for the
                                     C.P. Jayawardene Challenge Trophy for the Best Senior Scout Troop. The competition was held at the Kaduwela Scout 
                                     Training grounds from the 17<sup>th</sup> to the 18<sup>th</sup> of July, 2007. The champion Mayor’s Flag team comprised
                                      R.P. Indushan (P.L.), C.D. De Mel (A.P.L.), T.A.G. Fernando, D.L.R. Goonewardena, S.C. Gajanayaka, M.F. Faizal, B.P. Ranwala and D.G.D. Colonne.




                                  <p class="text-dark text-justify text-4">Six Senior Scouts from College represented Sri Lanka at the 21<sup>st</sup> World Scout
                                     Jamboree held from the 26<sup>th</sup> of July to the 8<sup>th</sup> of August 2007 in London to commemorate 100 years of Scouting. They 
                                     were J.D.E. Ranasinghe, R.A.I. De Fonseka, N.D. Bastions, M.G.S. Peiris, M.H.G. De Silva and T. Sellahewa. This Jamboree
                                      brought together more than 55,000 Scouts from around the world and was the largest youth gathering in modern history.



                                  <p class="text-dark text-justify text-4">The Annual Colombo District Camporee was held from 21<sup>st</sup> to the 26<sup>th</sup> of August 
                                    at Vihara Maha Devi Park, Colombo. The College Scout Troop performed exceptionally well winning both
                                     “The Best Gateway” and “The Best Camp Site” awards and received all four pennants for maintaining high camping standards.



                                  <p class="text-dark text-justify text-4">The Scout Troop became Champions for the 4<sup>th</sup> consecutive year in both the
                                     Scout and Overall categories in the Colombo District Swimming Meet which was held on the 29<sup>th</sup> of September at the
                                      Thurstan College Swimming Pool.
                                  </p> 




                                  <p class="text-dark text-justify text-4">Our Junior Patrol Leaders took part in the annual Patrol Leaders training camp held at 
                                    St. John’s College while three other Senior Scouts took part in the President’s Award training camp in Kaduwela.
                                  </p> 





                                  <p class="text-dark text-justify text-4">The Troop Community Service Project was held on the 24<sup>th</sup> of November
                                     and this year the Scouts took part in painting a building of the Uyana Kanishta Primary School in Lunawa. Some school
                                      items were also donated. Two more community service projects were organised at the Grace Perera Elder’s Home in Dehiwala and at a children’s home in Kotte.
                                  </p> 





                                  <p class="text-dark text-justify text-4">The Troop produced two President’s Scouts, one Chief Commissioner’s Award winner 
                                    and three District Commissioner’s Award winners during the course of this year. The investitures of sixteen recruits took place 
                                    in August. During this year our Scout Master completed his phase 4 training course.
                                  </p> 


                                   <p class="text-dark text-justify text-4">We take this opportunity to extend our gratitude to the 16<sup>th</sup> Colombo Group Committee, Instructors and
