                            <p class="text-dark text-justify text-4 text-start">
                             <span class="font-weight-bold" style="text-decoration: underline; color: black;">Term 1: 
                           
                            </p>
                             
                             

                            <p class="text-dark text-justify text-4">The dawn of the last year in this millennium was
                               another year full of expectations for the 16<sup>th</sup> Colombo Scouts. At the beginning of this term we had a
                               visitor, a fellow Scouter Pete Duchesne from the 65th Ottawa Scout Group of Canada. His visit
                              strengthened the brotherhood among the scouts of two nations.
                           </p>


                             <p class="text-dark text-justify text-4">We had scouting as usual throughout this term the major event was the 
                               Training Camp at Mirigama where our scouts were given a proper training for the forthcoming Camporee.
                           </p>



                           <p class="text-dark text-justify text-4">S.S. De Alwis of our troop who participated in the World Jamboree in
                              Chile returned home at the beginning of this term. Some of our scouts, l.U.P. Surriyapperuma,
                               M. Mallawarachchie, C. De Alwis, N.O. Dias, K.P.L. Perera, D.N. Peter, S.N. Peter and A.A. Farook
                              won their President Scout parchments during the course of this term. We would like to congratulate
                              our Master in Charge and G.S.L. Mr. Daniel Edirisinghe who won the Silver Lotus and Bar to the Silver
                              Lion awards for his services for the Scout movement.
                           </p>



                            <p class="text-dark text-justify text-4">Our scouts Participated in the campfire organized by the guides
                              of Vishaka Vidyalaya. They also participated in the B.P.’s day celebrations held at various places in
                              the district. It is very encouraging to note that our scouts provided the opening sequence for the
                              Science Day of College which was held during this term. Finally we would like to thank our Master in
                              Charge and G.S.L. Mr. D. Edirisinghe, Scout Leader Mr. J.P. Dharmasena and the A.S.L.s for all their
                              support and guidance given to us at all times to mold our troop to success.
                           </p>



                           <p class="text-dark text-justify text-4 text-center text-decoration-underline">
                             <span class="font-weight-semi-bold">19<sup>th</sup>  World Scout Jamboree – 1998/9</span>
                           </p>


                           <p class="text-dark text-justify text-4">S. Sanil De Alwis of the 16<sup>th</sup> Colombo Scout Troop represented Sri Lanka 
                             in the 19<sup>th</sup> World Jamboree Chile. A World Jamboree is held only once in four years and it is indeed
                              a rare opportunity to participate in one.
                           </p>


                           <p class="text-dark text-justify text-4">The Sri Lankan contingent which comprised 10 Scouts and 1 leader left Katunayake
                              on 26<sup>th</sup> of December 1998. Our first destination was Singapore then we flew to Kuala Lampur. Then we
                              landed twice in South Africa – Johannesburg and Cape Town. Then we flew to Buenos Aires and Argentina.
                               Our last stop was Chile. We reached Chile at around 10.00 p.m. on 27<sup>th</sup> December. We were greeted by
                               the Jamboree officials. There was a special gate for Jamboree participants in the Airport and were
                               taken to the campsite in a bus.
                           </p>


                           <p class="text-dark text-justify text-4">The Jamboree was from the 28<sup>th</sup> of December ‘98 to the 5<sup>th</sup> of January ‘99.
                              There were over 50,000 participants including camp officials. The whole campsite was divided no
                              24 Sub Camps. There were Supermarkets, Banks, Telephone booths, Souvenir Shops inside the campsite. 
                               The theme of the Jamboree was ‘Building Peace Together.’
                           </p>



                           <p class="text-dark text-justify text-4">We had a Hike, a Community Service Project, some Global Development programmes and a tour
                              to some factories in Chile. All these activities were done sub camp wise.
                           </p>


                           <p class="text-dark text-justify text-4">After the camp was over we were handed over to some families. We were taken for tours 
                                around the city by them they taught us about their customs. They were very friendly people.
                           </p>


                           <p class="text-dark text-justify text-4">We left Chile on the 10<sup>th</sup> of January. We stopped at Malaysia for three days before
                              we returned to Sri Lanka on the 15<sup>th</sup> of January.
                           </p>


                            <p class="text-dark text-justify text-4 text-start">
                             <span class="font-weight-bold" style="text-decoration: underline; color: black;">Term 2: 
                           
                           </p> 


                            <p class="text-dark text-justify text-4">The second term was another eventful term for the 16<sup>th</sup> Colombo scouts. During this term our 
                                scouts organized a project named “Luau”. This was the “Cook-it-Out” project which was held for the 
                                2<sup>nd</sup> consecutive year. This time it was held on a Hawaiian style.
                            </p>

                            
                            <p class="text-dark text-justify text-4">The scouts participated in a Campfire organized by the scouts of Royal College and in a one day program 
                                organized by the Girl Guides of St. Bridget’s Convent.
                            </p>


                            <p class="text-dark text-justify text-4">The most important event for this term was the Camporee. A lot of preparations were made in order to make
                              this event a success. In fact it was our troop who gained the most points to come first and win
                              pennants for all inspections.
                           </p>


                           <p class="text-dark text-justify text-4">A special mention should be made of one of our Asst. Scout Leaders B.R. Kulapala who was presented the 
                              Wood Badge – the highest award for a Scout Leader.
                           </p>



                           <p class="text-dark text-justify text-4">In conclusion we would like to thank our Master-in-Charge & Group Scout Leader Mr. D. Edirisinghe, 
                             Scout Leader, Asst, Scout Leaders and Instructors for all their support and encouragement rendered to
                             us at a times.
                           </p>


                           <p class="text-dark text-justify text-4 text-start">
                            <span class="font-weight-bold" style="text-decoration: underline; color: black;">Term 3: 
                           
                           </p> 



                            <p class="text-dark text-justify text-4">This being the last term for the year 1999, and for this millennium, it was the last 
                                chance for the Thomian Scouts to show their colours in this millennium.
                            </p>



                           <p class="text-dark text-justify text-4">The cricket encounter between the scouts of S. Thomas’ College and Royal
                               College was held on the 2<sup>nd</sup> of October. However, fortune was not on our side this year as well, 
                               for both our senior and junior teams failed to regain the shields.
                           </p>




                            <p class="text-dark text-justify text-4">This year too we were able to organize a project equivalent to last year’s
                               “Sand Storm”. This year, it was named “De Ja ‘Vu” and was a resounding success.
                            </p>


                            
