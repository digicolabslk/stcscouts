                              <span class="font-weight-semi-bold">Extracts from the souvenir published for DE JA ‘VU 1999:</span>
                              </p>


                               <p class="text-dark text-justify text-4"><span class="font-weight-semi-bold">Limericks:</span> </p>
                                 

                              


                              <p class="text-center text-decoration-underline text-dark text-justify text-4">The Kingdom By the Sea!</p>


                               <p class="text-center  text-dark text-justify text-4">The storm of sand was kinds exciting,<br> 
                                    even though the guides had really bad timing,<br> 
                                    And Since it was mighty great fun,<br> 
                                   The lords planned on having another one.<br> 
                                </p>

                                <p class="text-center  text-dark text-justify text-4">The Lords of the 16<sup>th</sup>  city were always compared,<br> 
                                   So they did their best to keep their traditions unimpaired.<br> 
                                   Being the best was their main goal,<br> 
                                   But with really short king they made no score.<br> 
                                </p> 
                                   
                                   
                                <p class="text-center  text-dark text-justify text-4">So once the new year dawned the first thing they did;<br> 
                                  Was banish the short smurf ‘cause they were tired of looking down to speak to their king.<br> 
                                  Now without anyone on the mighty throne of the land;<br> 
                                  The lords kept on looking for a handsome young lad;<br> 
                                  As choosing a king was no easy task,<br> 
                                  ‘Cause the lords needed a king who could make his mark…<br> 
                                  Soon they came across the Tall Dark & Handsome lord,<br> 
                                  Whom they made their king without a second thought;<br> 
                                  Now with their life-long dream come true,<br> 
                                  The lords were happy ‘cause all this time they had a short king who was kind a yucky.<br> 
                                </p>    


                                <p class="text-center  text-dark text-justify text-4">So with the tall one in the high stand,<br> 
                                  The lords now fought to be the subjects of the land,<br> 
                                   The usual thing for a king is to get a prime-minister,<br> 
                                   But instead he ended up with one big joker.<br> 
                                </p> 
                                
                                
                                 <p class="text-center  text-dark text-justify text-4">Whenever the lords go on camp,<br> 
                                  This young lord makes dashers so they build choke and cramp,<br> 
                                  The “Mahakokiya” he’s called with much haste,<br> 
                                  Cause without his granny’s recipe book he makes a big waste…<br> 
                                </p> 
                                
                                
                                 <p class="text-center  text-dark text-justify text-4">The Sergeant at Arms of the great land;<br> 
                                  Thanks to his teeth in dark we know where he stands,<br> 
                                  When god created him all he could say,<br> 
                                   “Oh shit! I burnt another one quickly get me some white spray.”<br> 
                                </p> 


                                

                                <p class="text-center  text-dark text-justify text-4">He tries his luck fishing near B.C.,<br> 
                                  And ended up getting tangled up with a year 8 baby,<br> 
                                  Since that day he has been up on the moon,<br> 
                                  Never spoken to her, guess he better do it soon.<br> 
                                </p> 


                                <p class="text-center  text-dark text-justify text-4">The lord in charge of all the land’s money,<br> 
                                  Makes it a point to give to everybody (especially to the king),<br> 
                                  He thinks he can act like a babe in “Baywatch” emphasizing the word act,<br> 
                                  ‘Cause we’d hate to be the ones to watch,<br> 
                                  Last year he went to the great Chilean base…<br> 
                                </p> 


                                <p class="text-center  text-dark text-justify text-4">By the way, if you see a Huge pimple don’t worry,<br> 
                                 That’s just his so called cute face,<br> 
                                 The great lord of the Malwatte clan,<br> 
                                  Got a huge boot near a coca cola stand.<br> 
                                </p> 


                                <p class="text-center  text-dark text-justify text-4">Let bygones be bygones has to be the theme for this lad which our man finally did understand,<br> 
                                  At Camporee this year he made a big mess,<br> 
                                  Cause we found him sharing with Dhaman in the mesh (in the night),<br> 
                                  He always had a knack for getting caught in the act.<br> 
                                </p> 


                                <p class="text-center  text-dark text-justify text-4">Last time it was washing Issadeen at camp Pedro after the big splat,<br> 
                                  Now whenever you meet the lord on the street,<br> 
                                  Don’t forget to ask about Dharman and get yourself a treat,<br> 
                                  An engineer by trade who fixes pipes in the ground,<br> 
                                  Right now he threatens to bring M.C. down to the ground.<br> 
                                </p> 


                                <p class="text-center  text-dark text-justify text-4">Being Smurfs bro nothing much to say,<br> 
                                  ‘Cause to the level of the eye he never shall stay,<br> 
                                  My co-editor now here’s a man,<br> 
                                  and Drops his pen frequently so to bend he can.<br> 
                                </p> 



                                <p class="text-center  text-dark text-justify text-4">I still remember at Mirigama with his ants,<br> 
                                  Jumping up and down cause they were in his pants,<br> 
                                   God has not been fair to all ex-lords,<br> 
                                  ‘Cause this crummy fellow is of a quarter short…<br> 
                                </p> 



                                  <p class="text-center  text-dark text-justify text-4">At Sand Storm while with a byt he was dancing,<br> 
                                  Shall we stop for a drink she kept on asking,<br> 
                                  One at our lords went to pilot school to check on it,<br> 
                                   But her never got through ‘cause he was too fat to get in to the cockpit.<br> 
                                </p> 

                                 <p class="text-center  text-dark text-justify text-4">Another of our clan went a trek of a German land,<br> 
                                   And got busted for doing unnecessary things with his hands,<br> 
                                   We’ve got ourselves a kind of a byt and he’s a veggie,<br> 
                                   Knowing what he misses do we feel sorry!<br> 
                                   Once upon a time he had a crush on a girl,<br> 
                                    Knowing what he’s capable of, we know it was her father’s B.M.W. that actually made him twirl,<br> 
                                   He says he works out a lot with a flair,<br> 
                                    But if you see him you’d think that his life is so unfair.<br> 
                                </p> 


                                 <p class="text-center  text-dark text-justify text-4">Is it a man, a mountain – Oh! no its just a cow,<br> 
                                   Who saw a V.V. guide and all he could say was wow!<br> 
                                   Since that day he has been pumping iron,<br> 
                                   To impress his lady by being a nutty giant,<br> 
                                    Being a four legged creature he should have known better,<br> 
                                   ‘Cause now all his brains hare descended to his rear.<br> 
                                </p> 


                                 <p class="text-center  text-dark text-justify text-4">We’ve got ourselves a chap who tries to flirt everywhere but unfortunately to this day he has got nowhere,<br> 
                                    At a Enviro day at S.B.C. he got so chummy,<br> 
                                    And fell for a byt who wasn’t that yummy!<br> 
                                    Now he joins our Sergeant at Arms when going on his walks,<br> 
                                    ‘Cause neither of them have the guts to talk.<br> 
                                </p> 


                                 <p class="text-center  text-dark text-justify text-4">The banished king of the 16<sup>th</sup>  tribe,<br> 
                                    wanted to make “0 i…” his so very bride,<br> 
                                    He bought her a pendant and got all ready,<br> 
                                    But in the end he never did give if cause he felt stingy.<br>  
                                </p> 


                                 <p class="text-center  text-dark text-justify text-4">I won’t say anything more to little smurf you,<br> 
                                   ‘Cause God definitely should care spent a little more time on you,<br> 
                                   In the city is an aged lord so shallow,<br> 
                                   If you knock on his head you’ll feel hollow,<br> 
                                   He finally came across his star crossed match,<br> 
                                    Sorry Besh cause once you go she’s gonna be my catch!<br> 
                                   Don’t worry Beshy boy I won’t play dirty,<br> 
                                   Even though you’re my friend it’s bound to get naughty.<br> 
                                </p> 


                                 <p class="text-center  text-dark text-justify text-4">Niro and Pasan two of our ex-lords,<br> 
                                   To talk of them oh how it bores,<br> 
                                   N’s the cheeky one who can’t blow a candle in one;<br> 
                                   While Pasan if not for his chest hair could have passed for a nun!<br> 
                                </p> 


                                 <p class="text-center  text-dark text-justify text-4">The R man and Pinky they were once called,<br> 
                                  Fired from the land cause they were getting too old,<br> 
                                   We do miss them once in a while,<br> 
                                  That is whenever we do the dishes which they did so fine!<br> 
                                </p> 



                                 <p class="text-center  text-dark text-justify text-4">One of our lords got something wrong with his tummy,<br> 
                                   ‘Cause whenever we go out to eat he acts kinda funny,<br> 
                                   When he’s with us no food shall he eat but in private my my what a treat!<br> 
                                </p> 


                                   <p class="text-center  text-dark text-justify text-4">Another black lord do we have,<br> 
                                   All he does is try to mix music whenever he can.<br> 
                                   While there’s another one who crossed over from the preppy land,<br> 
                                   He now knows what he had missed says this rat of a lad…<br> 
                                </p> 

                                        
                                    <p class="text-center  text-dark text-justify text-4">Well that’s all I’ve got to say for now,<br> 
                                   ‘Cause being a king makes me quite busy somehow,<br> 
                                   I’m sure the other lords will try to get at me for what I have just said;<br> 
                                   Let me warn you guys, treason is more then enough for ya’ll to lose your head!<br> 
                                   The King has spoken!<br> 
                                </p> 



                                <p class="text-dark text-justify text-4"><span class="font-weight-semi-bold">Articles:</span> </p>


                                 <p class="text-center text-decoration-underline text-dark text-justify text-4">Hmmm! Now let me see…</p>


                               <p class="text-dark text-justify text-4">Ah! yes! it all began when our very own Beshy Boy came up with
                                 yet another of his bright ideas, and this time it was a cycle hike. And we being the brave type who 
                                 would try on anything… or should I say the stupid type nah! We agreed whole heartedly.
                               </p>


                                 <p class="text-dark text-justify text-4">Well it started off with a bang all right, ‘Cause Malla
                                     almost got whacked by a bus while crossing the road (luckily for him and definitely not for us… just
                                      kidding) nothing happened. Anyway, that being over with, we started towards Rathmalana, and being the
                                       “Happy Hour” – (that is when vans full of babes go about) we were getting that “Hmm! Who goes there?…”
                                        look from all over and being a bunch of major hunks on bikes we should have expected it.
                                </p>



                               <p class="text-dark text-justify text-4"> I’m sure all you guys don’t wanna listen to how we crossed ferries
                                 or how we climbed mountains, however I’m sure you would like to know how we were cursing each other
                                  for their bright idea of this cycle hike ’cause we have been riding for more than five hours and
                                   still hadn’t reached our destination. Going further and further out of Colombo we were already 
                                   missing those beautiful young things so all we could do was pinch each other’s bums and surprise 
                                   each other naughty! naughty…!
                               </p>




                                 <p class="text-dark text-justify text-4">Finally, here we were at Horana. Our legs were practically 
                                    giving away and our rear so sore that we couldn’t sit on it for a while. The first thing we did was 
                                    to go and jump in the lake, well at least all of us ’cause O.K. our man or better known as “Jinjy”
                                     was allergic to H2O. Well that night we all slept like babies well at least I know that I did ZZZ!
                                </p>




                               <p class="text-dark text-justify text-4">The next day we were all up and ready to spend all day long 
                                doing nothing but talk, sleep or “oblix”. Well we always had the option of going down to the river but
                                 then we postponed it until evening. However keeping our bowels open was an necessity and Voila! We got
                                  ourselves a bog – island just for our own use. Well our very own puksy never did like the idea – as
                                   the saying goes “When the going gets tough the Thoras get going” that’s exactly what we planned on 
                                   doing well that was the last night of our not so small hike, and to spend it we started our routine
                                    discussions on “you know what”. Well it didn’t last long ‘cause “Happy” was missing a slipper and
                                     we went again wrestling for each other’s slipper man was that fun or what!
                                </p>



                                <p class="text-dark text-justify text-4">That night all we did was “oblix” and frolic away. The next
                                     day we got on our mighty stallions (the bikes that is) and rode our way back to home sweet home.
                                      Well the cycle hike was a memorable one for all of us, for some it was a life and death matter and 
                                      for another it was a toilet issue… But for most of us it was being together and doing fun things as 
                                      a team, a group of friends and being there for each other ‘cause when all are done and are over with;
                                       it is these things that are left in a special place in our hearts as it says in our College 
                                       Song – “For all we have and are”. Esto Perpetua.
                                </p>




                                <p class="text-dark text-justify text-4">Someone proved worse ‘cause he ended up getting his hand cut to
                                     a bamboo split and you should have seen him saying, “I told you no Machang, I didn’t want to do it
                                      there in the first place.” Hehe! Well that was just the beginning of our troubles.
                               </p>




                               <p class="text-dark text-justify text-4">Next all of us went for a dip in the water, and being dingbats 
                                we wanted to cross the river (Real fun eh!) we didn’t last long ’cause suddenly someone shouted “eh!
                                 machang!!! Malwatte na!” oh my gosh! And all of us just panicked we were all by ourselves – no teachers
                                  – no grownups – and if something happens to him we’re all done for. Suddenly I see him clinging on to
                                   our very own smurf’s hand and down he goes again… All I could do was reach into the water and take him
                                    out from his neck and this grateful fellow in return spits right on my face so much for returning a 
                                    favour eh! I still remember this chap screaming – well it wasn’t exactly funny then. Well finally
                                     getting the fellow on the boat, we got him to shore and tried same C.P.R. (well- no- mouth to mouth 
                                     business – would have been different if it was a babe – for sure!) He didn’t need it any way well 
                                     to be quite frank all of were quite shaken after this scene – pretty bad one too.
                               </p>



                               <p class="text-dark text-justify text-4 text-end">
                                  <span class="font-weight-semi-bold">Thusitha<br>  The Don Man</span>
                              
                                </p>


