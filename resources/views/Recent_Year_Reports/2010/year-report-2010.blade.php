                              <p class="text-dark text-justify text-4">The 16<sup>th</sup> Colombo Scout Troop had a very productive 
                                and eventful year, having attained many awards and keeping the Thomian flag flying high.
                                 The troop consists of 60 scouts, a scout leader and two assistant scout leaders.
                              </p>


                              <p class="text-dark text-justify text-4">The troop commenced its year by organizing a recruitment
                                 programme, which was planned out and implemented to encourage more students to join scouting. 
                                 This was a worthy effort as it proved to be productive and effective.
                              </p>


                              <p class="text-dark text-justify text-4">The 16<sup>th</sup> Colombo Scout Group was chosen by the Chief 
                                Commissioner as the role model group in the island for the Asia Pacific regional committee 
                                visit on the 18<sup>th</sup> of January, 2010. An interesting programme was organized which included a
                                 going-up ceremony, a beneficial interactive session between the visitors and the scouts, and
                                  a cultural show.
                              </p>


                              <p class="text-dark text-justify text-4">A patrol leaders’ training camp was held from 22<sup>nd</sup> to 24<sup>th</sup> of January 
                                2010 at Vidura College which concentrated on improving leadership qualities of the scouts. Seven scouts from 
                                the troop attended this programme.
                              </p>



                              <p class="text-dark text-justify text-4">The Troop Night was held on the 5<sup>th</sup> and 6<sup>th</sup> of March, 2010. The programme 
                                consisted of scouting activities and fun-filled games. Three past scouts of 16<sup>th</sup> Colombo donated three tents to the
                                 troop, on the 6<sup>th</sup> of March, which were a very useful addition to the troop’s equipment.
                              </p>




                              <p class="text-dark text-justify text-4">The annual troop camp was held from the 28<sup>th</sup> of April to the 1<sup>st</sup> of May in Mankulam, 
                                Jaffna. The five day camp saw the participation of over 30 scouts and enabled the scouts to visit some of the historical places 
                                in the North. The highlights of this camp were the interactive session with the students of Mallavi Central College and having a campfire with them.
                              </p>



                              <p class="text-dark text-justify text-4">The Scouts’ Service & Relationship week, widely known as “Job Week”, was held from 
                                the 5<sup>th</sup> to 13<sup>th</sup> June. The 16<sup>th</sup> Colombo Scout Group became the highest collector in the Colombo district.
                              </p>



                              <p class="text-dark text-justify text-4">A training camp for both the C.P.J. and Mayor’s Flag teams was held from 24<sup>th</sup> to 27<sup>th</sup> of June 2010,
                                 in order to train the teams for the competitions. A seniors’ camp was held on 17<sup>th</sup> July 2010, which was mainly focused on training the senior
                                  scouts in various scouting skills.
                              </p>




                              <p class="text-dark text-justify text-4">The prestigious Brigadier C.P. Jayawardene Challenge Shield and Mayor’s Flag competitions were held
                                 on 9<sup>th</sup> August 2010, at Vihara Maha Devi Park. The troop managed to emerge champions in both these competitions. The C.P. Jayawardene Challenge
                                  Shield was won by the troop for the second consecutive year and the Mayor’s Flag was won for the fifth consecutive year.
                              </p>


                              <p class="text-dark text-justify text-4">The annual Colombo District Scout Camporee was held from the 12<sup>th</sup>  to 16<sup>th</sup>  August 2010, at the Vihara Maha
                                 Devi Park. The troop performed exceptionally well winning two pennants and also winning the ‘Best Camp-Site‘ and ‘Best Kitchen‘ awards.
                                  Around 100 cub scouts took part in the Cub Scouts’ day at the Camporee.
                              </p>



                              <p class="text-dark text-justify text-4">The scouts of 16<sup>th</sup> Colombo successfully completed the playground project where we had to construct a tower bridge structure 
                                for the lower school playground. Work commenced on the 25<sup>th</sup> August and was completed on the 6<sup>th</sup> of September 2010. The World Clean-Up Day programme was held on the 25<sup>th</sup> of September, 2010.
                                 The troop had a special Saturday scouting programme in which the scouts cleaned a section of the Mount Lavinia beach.
                              </p>



                              <p class="text-dark text-justify text-4">The annual Colombo District swimming meet was held at St Joseph’s College, Colombo 10 on the 24<sup>th</sup> of October and 16<sup>th</sup> Colombo managed
                                 to emerge as the runners-up at this event.
                              </p>



                              <p class="text-dark text-justify text-4">The Troop participated in the 8<sup>th</sup> National Jamboree which was held 26<sup>th</sup> of December 2010 in Angunukolapalessa, Hambanthota. Thirty scouts camped at the
                                 jamboree and took part in various activities while maintaining good Camping standards.
                              </p>


                              <p class="text-dark text-justify text-4">Finally we would like to extend my gratitude to the Warden, Sub-Warden, Prefect of Games, Scout Master, Cub Scout leaders, instructors and parents for their continued
