                              <p class="text-dark text-justify text-4">The 16<sup>th</sup> Colombo Scout Troop of S. Thomas’ College
                                 had a very productive and eventful year this time too having attained many awards keeping
                                  the Thomian Flag flying high.
                              </p>


                              <p class="text-dark text-justify text-4">The Troop Sports Gala organized by the 16<sup>th</sup> Colombo Scout
                                 Group was held in school premises on the 11<sup>th</sup> of March 2008. This was an event organized for the 
                                 first time and saw the participation of the Scouts of S. Thomas’ Prep too.
                              </p>


                              <p class="text-dark text-justify text-4">The President’s Award Evaluation Camp organized by the district was held
                                 from the 4<sup>th</sup> – 6<sup>th</sup> of April in Kaduwela.
                              </p>


                              <p class="text-dark text-justify text-4">The Troop organized its holiday camp from the 16<sup>th</sup> – 20<sup>th</sup> of April where 
                                the Scouts camped at the Pedro campsite. Even though the camp was a holiday camp the Scouts were trained to maintain 
                                high camping standards as most of the campers were recruits of the Troop.



                              <p class="text-dark text-justify text-4">The District Sports Gala was held on the 17<sup>th</sup> and the 24<sup>th</sup> of May and Scouts of our Troop did
                                 exceptionally well to emerge as the “Overall Champions for the Bartleet’s Trophy”.
                              </p>




                              <p class="text-dark text-justify text-4">The Job Week was held from the 7<sup>th</sup> – 22<sup>nd</sup> of June and yet again the Scouts were able to collect over Rs. 250,000/- and 
                                were awarded the all-island award the “Highest Collectors for Service and Relationship Week” award for the year 2008.
                              </p>



                              <p class="text-dark text-justify text-4">The Colombo District Inter-Troop C.P. Jayawardena Challenge Shield and “Mayor’s Flag” competitions were held from the 4<sup>th</sup>-5<sup>th</sup> of July in Kaduwela.
                                 The Scouts of 16<sup>th</sup> Colombo were able to show that they were the best yet again by retaining the Mayor’s Flag for the third consecutive year. This year’s team comprised S.C. Gajanayaka
                                  (P.L.), M.F. Faisal (A.P.L.), N.J. Bulathsinhala, K.H.B. Hurulle, G. Neelaghandan, D.G.D. Colonne, B.P. Ranwala and K.A. Sudesh Sanjaya. The seniors too did well in securing the 3<sup>rd</sup> place at
                                   the C.P. Jayawardena Challenge Shield competition. Meanwhile the Cub Scouts participated in the C.M.P. Wanigathunga competition for the Best Cub Pack in the Colombo District and Team ‘A’ emerged
                                    champions winning the award along with a cash prize, while Team ‘B’ emerged 1<sup>st</sup> Runners Up.
                              </p>



                              <p class="text-dark text-justify text-4">The annual Colombo District Camporee was held from the 26<sup>th</sup> – 31<sup>st</sup> of August at the Viharamaha Devi Park. At this too, our Troop did really well 
                                to maintain the highest of standards throughout the Camporee. And as a result of this, we were awarded pennants at all sub-camp inspections and an extra pennant was awarded to the Troop 
                                for “The most innovative & original Troop of the Colombo District” with a cash prize awarded by Unilevers. The Troop was also awarded the District Commissioner’s Flag for the “Best Scout 
                                Group of the Colombo District” for the year 2007 along with a cash prize.
                              </p>




                              <p class="text-dark text-justify text-4">Towards the end of year, the District Swimming Gala was held at D.S. Senanayaka College on the 11<sup>th</sup> of October and we emerged as 1<sup>st</sup> Runners Up in the overall
                                 performance. Cub Scouts won the championship in the Junior Category.
                              </p>




                              

                              
                                      

                          

                           
                            
