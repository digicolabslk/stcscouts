                              <p class="text-dark text-justify text-4 text-start">
                             <span class="font-weight-bold" style="text-decoration: underline; color: black;">Term I </span>
                             </p>
                             



                              <p class="text-dark text-justify text-4">2018 marking a 100 years at Mount, 
                                also marked a busy start of the year for the scouts, seemingly almost immediately after the new Troop 
                                officials were appointed at the Court of Honour, which was held on the 6<sup>th</sup>  of January. They were tasked 
                                with the construction of the 100 years at Mount entrance which saw three renditions through three days of
                                 toil by the Senior Scouts, who were to have it prepared by the 27<sup>th</sup> .
                              </p>


                              <p class="text-dark text-justify text-4">With the exception of Saturday Scouting meetings, the next event on the
                                 calendar was President’s Evaluation Camp held from the 2<sup>nd</sup> to the 4<sup>th</sup> of March at Kaduwela. Six of our
                                  Senior Scouts attended this camp and went on to pass this evaluation process
                              </p>


                              <p class="text-dark text-justify text-4">Up next was the Troop Holiday Camp held at Bulathsinhala, Horana held
                                 from the 7<sup>th</sup> to the 11<sup>th</sup> of April. A total of 40 scouts attended the camp. The terrain and the environment
                                  of the campsite enabled the junior scouts to learn and develop their skills.
                              </p>


                              
                  

                        
                             <p class="text-dark text-justify text-4 text-start">
                             <span class="font-weight-bold" style="text-decoration: underline; color: black;">Term II </span>
                             </p>
                             



                              <p class="text-dark text-justify text-4">Saturday Scouting programmes were primarily focussed on during
                                 this term to capitalise on the new-found experiences of the scouts after the Annual Holiday Camp.
                              </p>



                              <p class="text-dark text-justify text-4">The Troop Night was held on the 8<sup>th</sup> and 9<sup>th</sup> of June at the Scout
                                 Room during which our Scouts developed many attributes such as leadership and teamwork. This was followed
                                  by a beach programme the next day.
                              </p>




                              <p class="text-dark text-justify text-4">On the 9<sup>th</sup> of June, 5 of our scouts were sent in response to the invitation for 
                                5<sup>th</sup> Colombo Ranger’s Company’s (Ladies College) event, titled ‘Imprint’. This event served as a means 
                                of strengthening the bonds between the Colombo Scouts and Guides, and had us spend the day taking part
                                 in various activities.
                              </p>


                              <p class="text-dark text-justify text-4">The Col. C.P. Jayawardena shield and the Mayor’s flag competitions, which are the most
                                 prestigious competitions for Scouts in the Colombo District, took place on the 23<sup>rd</sup>  and the 24<sup>th</sup>  of June
                                  at the Lee Dassanayake campsite in Meerigama. Our Senior Scouts and Junior Scouts trained tirelessly and 
                                  their efforts paid off. Both teams emerged victorious while the Col. C.P. Jayawardena Team emerged 
                                  champions for the 10<sup>th</sup>  consecutive year. The teams were as follows:
                              </p>

                             
                        


