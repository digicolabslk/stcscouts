                              <p class="text-dark text-justify text-4">The Public Service and Relationship Week commonly known 
                                as Job-week was held from the 23<sup>rd</sup> of June to the 1<sup>st</sup> of July. Once more, our troop emerged as the
                                 highest collectors in the island.
                              </p>


                              <p class="text-dark text-justify text-4">A new activity was introduced to the troop by Mr. Raveen, an instructor of the
                                 College troop, in the form of rifle shooting. This aimed to teach scouts how to develop firearms and have a better 
                                 understanding of the sport. As it stands now, there are plans to introduce scuba diving in 2019.
                              </p>

                               <p class="text-dark text-justify text-4">The main event for the Trinity term was the 53<sup>rd</sup> Colombo Camporee, which
                                 was held from the 10<sup>th</sup> to the 14<sup>th</sup> of August at the Viharamahadevi Park. Our scouts went through weeks 
                                 of toil preparing for the six-day camp. Fifty-four scouts represented our troop at this event and the
                                  troop performed exceptionally at this event winning both the pennants awarded at the Camporee. Also,
                                   majority of our scouts achieved the Camporee Award during this camp.
                              </p>


                              <p class="text-dark text-justify text-4 text-start">
                             <span class="font-weight-bold" style="text-decoration: underline; color: black;">Term III </span>
                             </p>

                              

                              <p class="text-dark text-justify text-4">This term proved to be one of the busiest as it saw the troop host the Annual
                                 District Swimming Gala, which was held on the 15<sup>th</sup> of September 2018. Though victory was not on our
                                  side, the event was still a resounding success, so far as to being known as one of the best District
                                   Swimming Gala to be organised. The success of this event is mainly attributed to the Group Committee 
                                   and the Old Thomian’s Scout Guild for their constant support and perseverance through the organising 
                                   process
                              </p>


                              <p class="text-dark text-justify text-4">Next came the biggest national event for the year; The President’s
                                 Scout Award Ceremony held on the 20<sup>th</sup> of October at the Mahinda Rajapaksa Auditorium in Kandy. A 
                                 total of 22 Thomian Scouts were awarded their President’s Scout Award at this ceremony.
                              </p>


                              <p class="text-dark text-justify text-4">The Troop Community Project also took place during the Michaelmas
                                 term on the 10<sup>th</sup> of November 2018, during which a beach clean-up was done at the premises of
                                  the Mount Lavinia beach. Our scouts worked tirelessly to clear the beach of plastic and other artificial
                                   materials. Overall the Community Service Project was a success.
                              </p>


                              <p class="text-dark text-justify text-4">The Going-up Ceremony, the moment in which a cub scout commences
                                 his journey as a scout, was held on the 27<sup>th</sup> of November, at which nearly 20 cub scouts were enrolled
                                  as Scouts. The new recruits were given an introduction to Scouting after which they participated in 
                                  some activities with their new patrols.
                              </p>



                              <p class="text-dark text-justify text-4">On the 1<sup>st</sup> December the Investiture Ceremony was held and 9 recruits were invested as Scouts in our troop.
                              </p>



                              <p class="text-dark text-justify text-4">The final event for the year was the Annual Patrol Camp, which
                                 was held in Horana from the 6<sup>th</sup> to the 9<sup>th</sup> of December. The camp was organised by the Patrol Leaders,
                                  giving them the opportunity to learn and experience the organisation of events and also to give the
                                   opportunity to the junior scouts to take up leadership to develop themselves as future leaders in the
                                    troop. Forty-one of our scouts took part in this camp. The scouts were able to learn and enjoy 
                                    themselves, making the overall camp a success. This was the last event on the calendar and with it 
                                    the year came to an end.
                              </p>



                              <p class="text-dark text-justify text-4">We would like to express our sincere gratitude towards our Group Scout
                                 Leader, Mr. Beshan Kulapala, our Scout Master, Mr. Vijith De Silva, the Group Committee, the Old 
                                 Thomian’s Scout Guild, the parents and the scouts, for assisting us throughout the year and being a
                                  pillar of support to the troop, to make this year a success.
                              </p>



                              
                    
                                                                                                      
                            
