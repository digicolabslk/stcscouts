                             <p class="text-dark text-justify text-4 text-start">
                             <span class="font-weight-bold" style="text-decoration: underline; color: black;">Term 1: 
                             </p> 

                           




                            <p class="text-dark text-justify text-4">The dawn of a new year for the scouts of 16<sup>th</sup> Colombo makes
                                 no difference, as for them it is yet another three hundred and sixty five days of fun filled
                                  adventure, and indeed term one made no difference.</p>



                            <p class="text-dark text-justify text-4">At the beginning of the year itself we had a visitor from Canada a
                                 fellow scout, Scouter Peter Duchesne of the 65<sup>th</sup> Ottawa Scout Group in Canada. The purpose of this 
                                 visit was to enhance the brotherhood among the scouts of Canada and Sri Lanka and we could say that
                                  his was very well achieved in the end.
                            </p>

                            <p class="text-dark text-justify text-4">On the 17<sup>th</sup> & 18<sup>th</sup> of January the Colombo District Scout Head Quarters 
                                had competitions for the Mayor’s Flag and the C.P. Jayawardena  Memorial  Shield awarded for efficiency
                                 in scouting. To complete these competitions, two teams were sent to represent 16<sup>th</sup> Colombo. At the end 
                                 of the two day competition, the senior team which comprised S.N. Peter, A.A Farook, D.T.T. Abeysekara,
                                  I.C Rajapakse, S.S. De Alwis, C.T Fernando, L.N. Fernando, M.N.R. Peiris, and M.M. Fonseka emerged
                                   first and was awarded not only the C.P.J. Shield but also the Best Senior Patrol Colombo district.
                                    The juniors also fared well by coming second in their competition.
                            </p>

                            <p class="text-dark text-justify text-4">The scouts of Sri Lanka witnessed the 5<sup>th</sup> National Jamboree this year. Thirty Five 
                                Thomian scouts participated at this great occasion. The hard work put by all our scouts at the 
                                Jamboree was not in vain as we came first in the Colombo Contingent on all four days of inspection.
                                
                            </p>

                            <p class="text-dark text-justify text-4">During the term we were invited by the Girl Guides of Visakha Vidyalaya for their campfire
                                
                            </p>

                            <p class="text-dark text-justify text-4">All in all, Term 1 was a very active one for the Thomian Scouts.</p>

                           <p class="text-dark text-justify text-4 text-start">
                             <span class="font-weight-bold" style="text-decoration: underline; color: black;">Term 2:
                             </p>

                            <p class="text-dark text-justify text-4">As usual the 2<sup>nd</sup> term too proved to be a hectic term for the Thomian Scouts. On the 
                                23<sup>rd</sup> of May the Thomian Scouts organized Cooks’ Day which was named “Cook-It-Out II”. The girl 
                                guides of Visakha Vidyalaya, Ladies’ College and St. Bridget’s Convent participated in this activity.</p>


                            <p class="text-dark text-justify text-4">In the evening of the same day we screened “Titanic” – the award winning film in the College Hall and everyone present enjoyed the show</p>


                            <p class="text-dark text-justify text-4">Mr. Neville de Alwis who was our Warden for over 15 years and who had done so much for us retired 
                                on the 30<sup>th</sup> of June, and as a mark of appreciation we held a felicitation dinner on the 23<sup>rd</sup> of June. 
                                We also took this opportunity to present to him some tokens of appreciation and among them was a 
                                statue which was carved by our scouts.</p>

                            <p class="text-dark text-justify text-4">The annual Royal Thomian Scout Cricket match was held on the 7<sup>th</sup> of July at the Small Club Grounds. 
                                Unfortunately the Royalists       proved to be better by winning both matches.</p>

                            <p class="text-dark text-justify text-4">During the term we were invited for the campfires of Musaeus College, Royal College and St. Joseph’s College and our scouts participated.
                            </p>

                            <p class="text-dark text-justify text-4">One of our senior scouts, D.T.T. Abeyesekera was chosen as a member of the official 
                                Sri Lankan contingent to represent the country in the 12<sup>th</sup> Nippon Jamboree held on the 3<sup>rd</sup> to the
                                 8<sup>th</sup> August in Moriyeshi, Japan. We wish him the best of luck.
                                
                                </p>


                               <p class="text-dark text-justify text-4 text-start">
                             <span class="font-weight-bold" style="text-decoration: underline; color: black;">Term 3: 
                             </p>
    


                            <p class="text-dark text-justify text-4">The third term was a rather hectic term for
                                 the scouts of 16<sup>th</sup> Colombo. After camping five whole days at Guruthalawa S.T.C. the scouts had to
                                  come to school and get on to work at once as the Job week was around the corner. This year we 
                                  collected over Rs. 110,000 and thereby came second in the whole of Colombo District.
                            </p>


                            <p class="text-dark text-justify text-4">The scouts also participated in the Colombo District Rally
                                 which was held at the Vihara Maha Devi Park. Here we were awarded the C.P. Jayawardena Memorial 
                                 Shield and the Gulam Hussein Flag for producing the most number of President’s Scouts during the year.
                            </p>


                            <p class="text-dark text-justify text-4">We also took part in the Jamborees on The Air (JOTA) and Internet (JOTI) which helped us make new bonds with other scouts around the world.
                            </p>


                            <p class="text-dark text-justify text-4">The main project for 3<sup>rd</sup> term was “Sandstorm”, a joint venture
                                 done by us and the girl guides of Musaeus College, Col.07. This was the first time ever that scouts
                                  and guides undertook such a tedious task together. However the two day program proved to be a 
                                  huge success in the end.
                            </p>


                            
