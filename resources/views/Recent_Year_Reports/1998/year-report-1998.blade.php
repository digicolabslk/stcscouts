                            <p class="text-dark text-justify text-4">During the term we were invited by the Josephian Scouts for their carol 
                                service. During the vacation, we had the Troop Night which was yet another night full of fun activities
                            </p>

                             <p class="text-dark text-justify text-4">The juniors also had Patrol Camps at the Scout Room. The seniors
                                 however wanted more adventure in their lives so they went on a cycle hike to Randiyebay Estate in Horana.
                            </p>

                             <p class="text-dark text-justify text-4">During the term two of our senior scouts, M.N.R. 
                                Peiris & D.T.T. Abeysekara finished all necessary requirements for the President’s Award. We congratulate
                                 them on their achievement. One of the other senior scouts S.S. De Alwis was chosen as a member of
                                  the official Sri Lankan Contingent to the 19<sup>th</sup> World Scout Jamboree held in Chile. We wish him all 
                                  the best.
                            </p>


                             <p class="text-dark text-justify text-4">Finally we would like to thank our Master-in-Charge and Group Scout
                                 Leader Mr. D. Edirisinghe, our scout leader Mr. J.P. Dharmasena, all our A.S.L.’s and all those who had the courage to make a difference for the betterment of “The Tribe of the Evening Star”.
                            </p>

                          

                            
