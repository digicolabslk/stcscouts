                              <p class="text-dark text-justify text-4">Possibly the most important and final event of the term was the 50<sup>th</sup> Colombo District Scout
                                 Camporee. Being a camp that spanned for five days from the 26<sup>th</sup> to the 30<sup>th</sup> August at Shastrananda
                                  Maha Vidyalaya, Dehiwela. The Scout Troop was able to build prominent camping gadgets, earning 
                                  credit for their skills. The troop was able to showcase high camping standards yet again and were
                                   awarded two Golden Pennants for this.
                              </p>


                              



