                              <p class="text-dark text-justify text-4">The next event in the Scout calendar was the World Scout Jamboree 
                                held from the 26<sup>th</sup> July to the 8<sup>th</sup> August at Yamaguchi, Japan. The World Jamboree is held once in 
                                four years and the Scouts gained a whole new experience. Approximately 34,000 Scouts & Leaders participated
                                 all around the world. The Jamboree was a global village participants discovered and experienced and 
                                 learnt the integration of tradition, cutting – edge technology and peace building efforts. It was a 
