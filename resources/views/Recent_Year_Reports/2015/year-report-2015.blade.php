                               <p class="text-dark text-justify text-4 text-start">
                             <span class="font-weight-bold" style="text-decoration: underline; color: black;">Term I </span>
                             </p>
                             



                              <p class="text-dark text-justify text-4">The year commenced by felicitating the 19<sup>th</sup>
                                 Warden Revd. Marc Billimoria on the 20<sup>th</sup> January following with the Group Committee meeting and dinner.
                              </p>


                              <p class="text-dark text-justify text-4">The next event the Troop had to look forward was the
                                 St. John’s Ambulance Training Course on the 7<sup>th</sup> & 8<sup>th</sup> of February. Approximately 30 Scouts took part
                                  at this course and they were evaluated on the knowledge of First Aid. At the end of the day the Scouts
                                   received certificates from the St. John’s headquarters for completing the course.
                              </p>


                              <p class="text-dark text-justify text-4">The next event in the Scout calendar was the Colombo Scout Youth Forum held from
                                 the 27<sup>th</sup> February to the 1<sup>st</sup> of March at Meth Arena Centre, Athurugiriya. 8 Scouts from 16th Colombo
                                  Scout Troop took part at this event and gained several knowledge on innovative leadership, communication
                                   & social media and peace. One of a delegates W.H.S. Fernando was selected to be a part of the Colombo
                                    Scout Youth Council.
                              </p>


                              <p class="text-dark text-justify text-4">The next event was the Troop Movie Night. This was the first time an event of
                                 this sort was organized. Held on the 20<sup>th</sup> to 21<sup>st</sup> February, the Scouts and the recruits were shown
                                  “Night at the Museum” followed by a BBQ dinner. This event was greatly enjoyed by the Junior Scouts 
                                  and led them to involve more in Scouting.
                              </p>


                              <p class="text-dark text-justify text-4">The event that brought conclusion was the Training camp organized after 
                                a lapse of 5 years. The camp held at the Lee Dassanayake Scout Centre, Meerigama from
                                 the 22<sup>nd</sup> – 26<sup>th</sup> April. Sincere thanks goes to our new Scout Master Mr. Vijith De Silva for the support
                                  given throughout the camp. This camp turned out to be a tremendous success as it provided unforgettable
                                   memories.
                              </p>


                              <p class="text-dark text-justify text-4 text-start">
                             <span class="font-weight-bold" style="text-decoration: underline; color: black;">Term II </span>
                             </p>

                             



                              <p class="text-dark text-justify text-4">The second term began with the District Avurudhu
                                 Celebrations held on the 25<sup>th</sup> of May at Sports Ministry Grounds. The Scouts took part in many traditional
                                  games and bragged many prizes.
                              </p>



                              <p class="text-dark text-justify text-4">The next major event was the Scout Service and Relationship Week. After
                                 two weeks of hard work and sweat the Troop once again emerged the “Highest Collectors of the Island” – an 
                                 achievement that they have continued to retain for the past few years.
                              </p>




                              <p class="text-dark text-justify text-4">The next important event was the District competitions in which
                                 the Tropp has been triumphing for the past several years. Brig. C.P Jayawardena Challenge Shield for
                                  the Best Senior Scout Troop and Mayors’ Flag for the Best Junior Scout Troop, both held at Payagala, 
                                  Kalutara on the 13<sup>th</sup> to 14<sup>th</sup> June. The Scouts were tested on mapping, tracking, backwoods, estimation 
                                  etc. It is notable that the Mayors Flag team was able bring back the flag after a lapse of one year 
                                  proving that they would never give up. The Brig. C.P Jayawardena emerge victorious retaining the shield 
                                  for the 7<sup>th</sup> consecutive year. The both teams are as follows,
                              </p>

