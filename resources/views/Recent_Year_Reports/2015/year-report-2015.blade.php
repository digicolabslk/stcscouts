                              <p class="text-dark text-justify text-4">The next major event was the Annual Troop Community Service Project held
                                 on the 31<sup>st</sup> October at the Deaf and Blind School, Ratmalana. The project was an immense success by
                                  inviting many other schools. The Scouts were able to entertain the disable students while they 
                                  realised how fortunate we were born without illness. We would like to extend our gratitude to Mr. Ishan 
                                  Jalil who conducted useful sessions with the disable students. Overall the project was appreciated by
                                   all those who attended.
