                              <p class="text-dark text-justify text-4 text-start">
                             <span class="font-weight-bold" style="text-decoration: underline; color: black;">Term I </span>
                             </p>
                             



                              <p class="text-dark text-justify text-4">2019 started off with the appointments of the new Troop Officials
                                 at the Court of Honour, which was held on the 12<sup>th</sup> of January. The first event for the year was the
                                  President Evaluation’s Camp held from the 15<sup>th</sup> to the 17<sup>th</sup> of February at the Lee Dassanayake Scout 
                                  Training Center, Meerigama. Our Senior Scouts attended this camp and went to pass this evaluation
                                   process.
                              </p>


                              <p class="text-dark text-justify text-4">Up next was the Troop Holiday Camp held at Pannala from the 6<sup>th</sup> to the
                                 10<sup>th</sup> of April. A total of 45 Scouts attended the camp. The terrain and the environment of the campsite
                                  enabled the Scouts to learn and develop their skills.
                              </p>


                        
                             <p class="text-dark text-justify text-4 text-start">
                             <span class="font-weight-bold" style="text-decoration: underline; color: black;">Term II </span>
                             </p>
                             



                              <p class="text-dark text-justify text-4">Saturday Scouting programs were primarily
                                 focused on during this term to capitalise on the new-found experiences of the scouts after the Annual 
                                 Holiday Camp.
                              </p>



                              <p class="text-dark text-justify text-4">Twenty of our Scouts represented Sri Lanka at the 24<sup>th</sup> World
                                 Scout Jamboree, which was held in West Virginia, USA from the 22<sup>nd</sup> of July to the 2<sup>nd</sup> of August.
                              </p>




                              <p class="text-dark text-justify text-4">The final event of the term was the Annual Patrol Camp, organised by the Patrol
                                 Leaders, in Kahawatta from the 15<sup>th</sup> to the 18<sup>th</sup> of August. The reason being to give them the opportunity
                                  to learn and experience the organization of events and also to give the opportunity for the Scouts to 
                                  take up leadership to develop themselves as future leaders in the troop.
                              </p>


    

                              <p class="text-dark text-justify text-4 text-start">
                             <span class="font-weight-bold" style="text-decoration: underline; color: black;">Term III </span>
                             </p>

                              

                              <p class="text-dark text-justify text-4">This term proved to be the busiest term as it had most of the events scheduled.
                                 The term started off with the Public Service and Relationship Week known as Job-week being held from 
                                 the 13<sup>th</sup> to the 22<sup>nd</sup> of September
                              </p>


                              <p class="text-dark text-justify text-4">The Mayor’s Flag and Col. C.P. Jayawardena Shield competitions were scheduled 
                                next on the District calendar. The Junior and Senior Scouts respectively participated in the above
                                competitions.
                              </p>


                              <p class="text-dark text-justify text-4">On the 19<sup>th</sup> of September, the troop organised one of the most important event 
                                for this year, Escapade. It was a day full of adventure activities, which included Tube Rafting, 
                                Balancing Beams, Tarzan course etc. There were 38 teams which took part while the winner was crowned as
                                 champions and was rewarded with a cash prize of Rs.30,000. There was a mini carnival organised after 
                                 the competition where it attracted not only the teams but public as well. A mini event of this sort was 
                                 organised for the Cub Scouts named Escapade Kids. The small kids enjoyed their time and gained a lot of
                                  experience through this. This was an event organised by the troop after a lapse of many years, which was 
                                  appreciated by many after the conclusion of the event.
                              </p>


                              <p class="text-dark text-justify text-4">Next up was the District Scout Swimming Gala. The troop was once 
                                again given the privilege to host the event. It was scheduled on the 26<sup>th</sup> of September. Yet once again it
                                 was proved to be one of the best Swimming Gala organised in the recent past. Six of our Scouts won the
                                  prestigious President’s Award this year, thus bringing another year to an end.
                              </p>



                    
                                                                                                      
                            
