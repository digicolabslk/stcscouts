                              <p class="text-dark text-justify text-4">The first district competition for the year was the Cub and 
                                Scout Sports Gala for Bartleet’s Trophy. The Scouts did their best in the event and were able to 
                                become the second runners up. Soon after the Sports Gala, the Scouts had to take part in rendering 
                                service to the community as it was the Service and Relationship Week – commonly known as Job Week.
                                 The Group recorded a huge collection this year, and was recognized as the “Highest Collectors in
                                  the Island”.
                              </p>



                              <p class="text-dark text-justify text-4">Next in line were two most important competitions:
                                 C.P. Jayawardena Challenge Shield for the Best Senior Scout Troop and the Mayor’s Flag for
                                  the Best Junior Scout Troop, both of which were held at Science College on the 6<sup>th</sup> of July 2013.
                                   The Scouts were tested on tracking, mapping, compass marching, backwoods, cooking etc. and 
                                   recorded a noteworthy achievement by retaining the shield for the 5<sup>th</sup> consecutive year and the
                                    flag for the 8th consecutive year.
                              </p>




                              <p class="text-dark text-justify text-4">The last event for the term was the patrol camp organized by the patrol 
                                leaders which was held at Naula, from the 19<sup>th</sup>  to the 22<sup>nd</sup>  of August. A camp of this nature enabled
                                 the juniors to take part in active planning and also to gain confidence in them. This served as a 
                                 ground to train young Scouts in such aspects.
                              </p>


                              <p class="text-dark text-justify text-4 text-start">
                             <span class="font-weight-bold" style="text-decoration: underline; color: black;">3<sup>rd</sup> Term: </span>
                             </p>
                             <br>



                              <p class="text-dark text-justify text-4">Term 3 proved to be a relaxed one, but still a couple of competitions were left. 
                                In this term concentration was given for Saturday Scouting programmes with objectives of improving
                                 pioneering skills and ensuring that the juniors had fun.
                              </p>



                              <p class="text-dark text-justify text-4">The next major event for the term was the Community Service Project which was 
                                conducted at the Mihindu Sewa Sevana, a home for the disabled warriors. The Scouts were able to 
                                entertain the warriors while they realised the value of freedom and peace we all enjoy now and how 
                                fortunate we were to be born without illnesses. They also learnt that war is futile and destructs things
                                 that take ages to build.
                              </p>




                              <p class="text-dark text-justify text-4">Following this event, the Annual Cubs’ and Scouts’ Swimming Meet was
                                 held on the 9<sup>th</sup> of October at the Defence Services College. The Troop emerged runners up by the joint
                                  effort of both Cubs and Scouts.
                              </p>


                              <p class="text-dark text-justify text-4"> Towards the end of the year, from the 4<sup>th</sup> to the 8<sup>th</sup> of December, the
                                 50<sup>th</sup> Colombo Camporee was held at Science College. The Scouts exhibited their unparalleled pioneering skills 
                                 in constructing camping gadgets, the kitchen and the gateway. The Scouts were able to secure the 
                                 “Best Campsite” pennant and “Best Performance at Inspection” pennant
                              </p>



                              <p class="text-dark text-justify text-4">The final event for the year was caroling. This was the first time an event
                                 of this sort was organized by a Scout Troop in the island, and was a fresh and “out of the box” 
                                 experience for all involved. Scouts went around Colombo spreading the Christmas spirit by singing 
                                 carols at friends’ and well-wishers’ houses.
                              </p>



                              <p class="text-dark text-justify text-4">Our Most sincere thanks go out to the Warden, Sub warden, Group 
