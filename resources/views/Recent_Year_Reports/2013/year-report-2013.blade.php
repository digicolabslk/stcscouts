                              <p class="text-dark text-justify text-4">The year 2013 was yet another eventful and successful
                                 year for Scouting at College. The first task for the year was to increase the number of
                                  Scouts in the Troop. This was achieved through the distribution of illustrative notices
                                   to the middle school students which later saw an increase in numbers.
                              </p>


                              <p class="text-dark text-justify text-4">The first event on the calendar was the re-launching
                                 of the Rover Scout Troop: a segment of the Troop which consists of past Scouts who provide
                                  advice and function independently taking part in various activities initiated by the headquarters 
                                  and also organizing programs for the benefit of Scouts. A Rover Crew had existed in the 1970’s
                                   in the Troop, and had later died away due to various reasons. Hence, a new Rover Crew was freshly
                                    formed with the Scouts who had recently left the Troop. This event was held on the 9<sup>th</sup> of
                                     February. At this occasion, a new Scholarship for the Scouts was initiated in memory of
                                      Mr. Wilson Muttiah, a long-standing Scoutmaster of the Troop to recognize the 
                                      “Most Deserving Scout”. S.C. Gajanayaka was awarded the first Scholarship in this regard. 
                                      This award is to be an encouragement for the Scouts to perform better in the future.
                              </p>


                              <p class="text-dark text-justify text-4">The next major event for the term was the holiday camp organized
                                 by the Troop which was held in the hilly regions of Sri Lanka, Hatton from the
                                 27<sup>th</sup> of April to 1<sup>st</sup> of May. A holiday camp of this sort was held to take the brotherhood to a whole new level.
                              </p> 


