                            <p style="margin:10px 0 0 0; font-weight:bold; font-style:italic; color:black; text-align:center;">
                              Article from the souvenir of TIDAL WAVE 1996:
                            </p>
                            <br>




                            
                            <p class="text-dark text-justify text-4">It was my first troop meeting and already I had asked myself
                              a dozen times what on earth I was doing there. Here I was, barely turned eleven, on a Saturday morn
                              with a bunch of strangers, most of them a good couple of years older for my liking. Not a familiar 
                              face in sight. There seemed to be a great deal of almost military-like organisation which 1 didn’t
                              quite fancy. It was a day full of standing at attention, forming straight lines and indulging in weird 
                              games and competitions which left me somewhat confused to say the least. For instance I couldn’t
                              comprehend why there were ten different ways of tying two ropes together. And why did I have to write
                              down everything 1 did in this log book thing? Perhaps it was some sort of Boy Scouts insurance policy
                              which I could publish it in the form of an autobiography or sell to a recycling plant. Either way I
                              would make some money in my old age. And why did this guy who calls himself the Patrol Leader never 
                              seem to know the bliss of a nice Saturday morning full of cartoons and potato chips All in all it 
                              was a strange, unusual, queer, and yes, weird experience. I went home, and I didn’t really 
                              have much to say about that first day. 
                            </p>



                            <p class="text-dark text-justify text-4">For reasons beyond my knowledge I returned the following Saturday,
                              and the next, and the next, and the one after. I rediscovered each day that this Patrol Leader was of 
                              doubtful mental stability-until I realised that what he really wanted was to beat the other patrols at
                              all costs in whatever weird thing we did. What’s more, since he proclaimed to be our leader, we were
                              made to follow his fanatical ideals. I had heard of freedom of political expression, but this was 
                              ridiculous. 
                            </p>


                            <p class="text-dark text-justify text-4">The next few years found me engaged in a wide variety of
                              activities-creeping along slimy drains, commando-crawling along high-wires, lighting fires in places
                              where no sane person would, making tea in a paper bag, chanting Zulu stanzas which were supposed to 
                              bring forth rain and making blow-guns out of stems of papaya leaves. I also forged strong bonds of 
                              friendship, which, quite unlike the ones back in the classroom, were the result of some great unifying 
                              cause-compatible personalities, mutual interests, covering up for one another, joining forces to figure
                              out a way out of the constant infringement upon our civil liberties by the seniors, sharing clean
                              handkerchiefs during inspections, and the ability to extract intoxicating dosages of humour from any 
                              given situation.
                            </p>



                            <p class="text-dark text-justify text-4">We were brothers, comrades, soul mates, men with a mission. Not 
                             that we little brats amounted to anything substantial anyway but that’s beside the point. 
                            </p>



                            <p class="text-dark text-justify text-4">Our very first troop meeting-that was over a decade ago. When
                              we pause awhile every now and then to look back at life’s trail we’ve just trekked, what we see first
                              and foremost are not the night clubs, parties and those sizzling teenage years, but rather the few 
                              simple yet immeasurably valuable aspects which were ours because of the Scout movement-a wealth of 
                              unique experiences, those same bonds of friendship only strengthened and enriched even further, and many
                              vivid memories of the good times to escape into for a few moments of supreme solace and tranquility. 
                            </p>



                            <p class="text-dark text-justify text-4">I remember it all just like it was yesterday. The Man-Beast (who at
                              the tender age of eleven climbed the tallest of trees to reach the sanctuary of the topmost branches
                              just to avoid a uniform inspection), produced a piece of coconut which he claimed to have been lodged 
                              between his teeth for) two days, much to the disapproval of the Owl, who was convinced that the Man-Beast
                              was indeed more beast than man. That I thought it felt to disturb the Owl’s peaceful slumber at three
                              in the morning in my quest for a mosquito coil didn’t help much either. The Owl in retaliation 
                              accounted for the loss of a very valuable piece of cooking utensil belonging to the Man-Beast (more 
                              correctly to Man-Beast’s mother which was apparently gifted to her on her wedding day). While
                              this act ensured that none of us could never again set foot in the Man Beast’s household, it was 
                              later discovered that the valuable object was sent on its way to the municipality’s garbage dump.
                              Strangely enough, (then again, not so), all this occurred during what was supposed to be a very
                              serious Senior-Scout competition where troops from all over vied for honours; it must be mentioned
                              that the Senior Patrol from S. Thomas’ College carried away the Brigadier C. P. Jayewardene Shield 
                              in ’92 despite this holiday-camp attitude adopted by its members. 
                            </p>



                            <p class="text-dark text-justify text-4">Then of course there was the bespectacled six-footer Gaja Man who would
                              make the prize specimen of a skeleton closet in any medical school. Although his real name denotes the
                              meaning ‘the compassionate one’, Gaja Man had skilfully mastered the art of inflicting pain on those 
                              around him, as the Man-Beast would amply testify, citing instances of broken noses, blood-drawing knife
                              attacks and a countless amount of bruises, sprains and human bites. Gaja Man was at his deadliest when
                              in possession of broomstick, and, much to the annoyance of the rest of us was capable of projecting a
                              public image of a Bodhisattva. His provocative manoeuvres were mostly directed against Bulla the 
                              Bulldog, who was always quick to respond in a most savage and murderous manner. On one occasion during
                              a Camporee, a salivating Bulla with blood-shot eyes was approaching Gaja. Man, menacingly swinging
                              a broom in a wide circular movement which would have brained a buffalo on contact. Gaja Man swiftly
                              took cover behind a tree, and with a cracking report Bulla’s broom struck the sturdy trunk,
                              generating a reverberating shock which towelled up and down Bulla’s arm as the
                              broom sprang into life and leaped out of his grasp. . Gaja Man had already began to celebrate as 
                              Bulla doubled over, panting and salivating, clutching his forearm in agony… 
                            </p>



                            <p class="text-dark text-justify text-4">An essential component of hitting something off well in our time was the
                              presence of Gala, who, being a master at photography, captured on film all our adventures and 
                              misadventures, Gala was the romantic among us, and would stare endlessly at a sunset in the western sky 
                              overlooking a lake in the mountains and claim to witness some mystic phenomenon which the rest of us were 
                              too blind to see. He was also gifted in projecting a very solemn, serious profile to the public; in camp
                              he transformed into a walking barrel of laughter, and no one told a story better than Gala; with all the 
                              spice and essence. I remember once when, upon hearing a loud splash during a bicycle-hike down south, I 
                              looked back to see a lone bicycle without its rider. There was no movement on the narrow track or in the
                              paddy fields on either side. Suddenly some shape materialized from underneath the mud and clay and 
                              pro­ceeded to crawl out of the paddy field much like a scene from a 960’s horror movie. Gala, however,
                              was in a very self-congratu­latory mood and argued that upon losing his balance h€ had the presence of
                              mind to jump feet first into the paddy field rather than have nature dictate terms where he would 
                              have fallen head first. The rest of us were paying scant regard to his point; we were busy trying to
                              catch our breath and rid out midsections of laughing cramps. On another occasion during a hike
                              through an enchant­ing mountainous forest at dawn I was treated to yet another new experience as I
                              beheld Gala suddenly stop dead in his tracks, lay down his gear, tackle the laborious task of
                              removing his haversack, dive deep into it to fetch his camera, proceed to load it while balancing
                              precariously on the edge of a steep trail, and with the utmost precision, patience and 
                              deliberation of a Shaoling priest, click into emptiness. He then dismantled his equipment, packed
                              his haversack and heaved it once more over his shoulders, apparently very pleased with himself.
                              Curiosity got the better of me and I approached the place where Gala stood but a moment ago
                              wondering what magical scene was worth all that trouble, and I almost missed the dew 
                              sprinkled cobweb among the orchids and lichens glistening gloriously in the morning sun
                              Nightfall in Camp always brought along its fair share of memor­able events. Fittingly, 
                              the Owl took command of most of the ‘missions’ in the dark hours, the execution of which,
                              more often than not, required the services of Magua the boat-man. His services were made 
                              available solely on the basis of self-preservation on the part of the Owl and definitely not
                              through a voluntary act by Magua’s own free will. (It should be mentioned that Magua the baby of
                              the gang, was to next exercise his own free will only upon the departure of the rest of us from 
                              school). As was evident on numerous missions such as squeezing lime juice into a snoring Boot Face’s
                              wide open mouth and pursuing some wanted man up a gigantic tree at midnight, the Owl certainly found 
                              it beneficial to­wards his physical well-being that Magua carry out his bidding. 
                            </p>



                            <p class="text-dark text-justify text-4">Slippers never failed to become a burning issue in every camp, what with all
                              of us possessing virtually identical pairs of good old Bata’s. With many claims of ownership to given 
                              pair of slippers, these disputes were rarely settled amicably, much to the entertain­ment of the onlookers.
                              But none became so etched in our memories than when Bulla confronted Boot Face in the forests of 
                              Nuwara­Eliya. Bulla, infuriated even further than Boot Face had remained calm and collected through 
                              the formal accusations, verbal abuse and threatening gestures, resorted to his own warped interpretation 
                              of equality and proceeded to rip the pair in question to pieces, putt­ing to shame his canine 
                              name-sakes. Boot face patiently sat through the whole exhibition, strolled over to where he had hidden
                              his own pair of slippers and walked away, whistling the tune of Some Guys Have All The Luck. 
                              Realization dawned upon Bulla that he had just destroyed his own property as he sulked away the rest 
                              of that day. 
                            </p>




                            <p class="text-dark text-justify text-4">It may seem to an observer that throughout our Boy Scout career we 
                              did nothing but relive our second childhood-battling each other with broomsticks, chasing some poor soul
                              up a tree and shredding our slippers to pieces. On the contrary, Scouting to us was a gateway to an 
                              entirely new dimension-one filled with the kind of adventure, fun and excitement which otherwise would not
                              have been ours. We’ve camped in the jungles, up in the hills, on the plains, on the roadside and on the 
                              remotest beaches. We’ve trekked, cycled and travelled almost everywhere in our beautiful little island.
                              We’ve crossed the toughest terrain, climbed the high­est mountains and ventured far and wide. Our young
                              eyes have seen much, our young hearts have learnt much, and through it all, laughter was a constant
                              melody. 
                            </p>




                            <p class="text-dark text-justify text-4">Scouting in itself was a great teacher. Seeds of loyalty, honour,
                              brotherhood and courage were sowed in our souls, and we were blessed with the stability of
                              self-dependence, the asset of quick, sharp minds and a wealth of experience embodied in many a lesson
                              of life. The way of the pioneers gave us the thirst for adventure; the teachings of the wise were our
                              guiding stars; in the great outdoors, our spirits were set free. Many were the lessons we learnt,
                              and those too in the most effective manner. If you forgot to cover the firewood you had chopped, 
                              an evening’s drizzle would mean that you’ll be spending a very cold and unpleasant night up in the 
                              mountains. If you weren’t meticulous about lashing that trestle bridge, chances are that you’d end 
                              up in the rapids below. If you didn’t pay much attention to your sense of direction you would realize 
                              too late that you’re utterly lost in God knows where and freeze or dehydrate into a sorry state as
                              the case may be. And if you skip your homework, the ocean which was a mere duck pond twenty yards
                              away will pay you a rude visit at high-tide, or a wild elephant may decide to make a midnight 
                              snack out of your shelter. True, these are not exactly what one may classify as everyday situations;
                              nevertheless, cultivating the habit of doing something carefully, methodically, thoroughly 
                              and with all your heart is bound to ensure the smooth running of day to day life. Human beings are
                              the nicest creatures on earth as long as everything goes their way. But when they’re up against 
                              the wall, when their plans don’s work, when the tide turns, when the odds are against them, in
                              the face of unexpected emergency-that’s when the metamorphosis takes place. It is under these 
                              circumstances that those armed with common sense and clear minds will make use of their ability 
                              and experience to surge ahead with a smile. This is where scouting and the great outdoors come 
                              into play. Scouts thus do justice not only to themselves but to society at large: the thousands
                              of lives saved worldwide by alert and efficient scouts stand testimony to this fact. It’s just
                              like what we loved to say-‘when the going gets tough, the Scouts get going’! 
                            </p>



                            <p class="text-dark text-justify text-4">Well, so much for the memories of this old timer. Lonely old timer,
                              I would say-with Gala, the Owl, the Man-Beast, Gaja Man, Magua – et – all scattered in the corners of
                              the world from Australia to India and tram England to Sri Lanka, pursuing higher studies in various
                              fields from engineering to commerce to the arts to military and defence, while I myself remain tucked 
                              away in the Caribbean studying the healing art. The great wheel of life turns on as it must-yet all 
                              the oceans and continents between us fail to disband this strong unifying factor which keeps our 
                              friendships flourishing. The memories of the good old days will forever be strong no matter where 
                              in the world we may be. Each time I remember those campfires among the hills where our laughter echoed 
                              late into the night beneath the starry skies, and those unforgettable moments amidst majestic 
                              mountains, enchanting waterfalls and forbidding, fascinating trails through the unexplored, I know that
                              the thoughts of my buddies are with me all the way, yearning for that day when we would all meet
                              again, to relive those mighty fine days. 
                            </p>


                             <p class="text-dark text-justify text-4 text-end">
                                  <span class="font-weight-semi-bold">Changa</span>
                              
                                </p>
                              


                

                           
                            
