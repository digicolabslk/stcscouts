                              <p class="text-dark text-justify text-4">First class scouts I.U.P. 
                                Suriyapperuma, K.P.L. Perera, D.N. Peter, M. Mallawaratchie, N.D. Dias and senior
                                 scout V.P. de S. Samarasinghe were among the 14 members of the contingent selected to
                                  represent Sri Lanka at the 17th Asia Pacific jamboree in Kangwon, South Korea (200 km from Seoul).
                                   We were fortuitous enough to receive the audience of Her Excellency the President who presented
                                    the Sri Lanka flag to be hoisted at the jamboree.
                              </p>


                              <p class="text-dark text-justify text-4">The 17<sup>th</sup> Asia Pacific jamboree was held at Sorak National Park 
                                in the Kangwon province of South Korea from the 17<sup>th</sup> to the 13<sup>th</sup> of August 1996. Over 15000 Scouts
                                 and girl scouts from 52 countries from the region took part in the jamboree. The motto “Save Nature
                                  Greener Tomorrow” was fitting indeed for modern times where development is taking its toll on the 
                                  environment and its inhabitants.
                              </p>


                              <p class="text-dark text-justify text-4">We had to take a flight to Hong Kong via Bangkok first as there was no direct flight
                                 to Seoul. Then we took flight to Kimpo international airport where we met our welcoming party. The
                                  whole journey from Colombo to Seoul took about sixteen hours and it took another five hours by
                                   coach to arrive at the jamboree site, where we were given temporary tents to sleep in. Next morning
                                    we were taken to our campsite, a journey which took half an hour by foot. We managed with our heavy
                                     luggage as we got a taste of Korean hospitality when some scouts offered to help us with our bags.
                                      That same day, the opening ceremony of the jamboree, which was a massive spectacle, was held.
                              </p>


                              <p class="text-dark text-justify text-4">Next day we were given passes in jamboree activities both in and 
                                off site activities which included paragliding, wind surfing, off road hiking and many activities
                                 which were on the rest of the jamboree. We started our  activities right after lunch which was a 
                                 “hamburger, fries etc.” type meal.
                              </p>



                              <p class="text-dark text-justify text-4">During the jamboree we were invited by many troops for meals or just to make friends. 
                                We also explored around the jamboree site which took about an hour by foot from one end to another. 
                                When walking around, sometimes you are mobbed by scouts who want to swap badges and souvenirs. 
                                 Most of them who couldn’t speak English would go 
                                 <span class="italic-text">"exchangee!!"</span> 
                                    or 
                                 <span class="italic-text">"got patchees!"</span> 
                                 which means “got any badges for exchange?”. 
                                 Most of the Korean scouts could not speak English at so we were forced to use sign language and learn a few Korean words in order to overcome the language barrier. 
                                 We made friends not only among Korean scouts but also with scouts from India, Pakistan, U.S.A. and many other contingents that participated.

                               </p>




                              <p class="text-dark text-justify text-4"> The campfire was held in grand style while a culture festival
                                 let us educate ourselves about different cultures of our neighboring countries. Our item was a “Devil
                                  Dance” which was quite realistic with which, we managed to share a part of our culture with them. 
                                  The closing ceremony was like a rock concert with two huge screens showing what’s happening on stage 
                                  and lights all around. There were two official speeches but right after the closing remarks there was 
                                  a musical show which got everybody dancing and singing along some with tears in their eyes as the 
                                  jamboree had come to an end officially.
                              </p>



                              <p class="text-dark text-justify text-4">Next morning everyone started packing after breakfast as the coaches
                                 were scheduled to leave the site in the morning. The mood was a sad one indeed as you could see 
                                 it in everyone’s eyes as they said good bye to each other. We were among the last to leave along 
                                 with some other foreign scouts.
                              </p>



                              <p class="text-dark text-justify text-4">We had to make a long journey back to Seoul by coach. Our
                                 destination was Gocheock Middle School in Seoul as their scout troop was supposed to host us during
                                  the rest of our stay. We were taken to the homes of the scouts (each Korean scout assigned to one of us),
                                   where we had to stay and live as they lived within a short radius of each other. During the home
                                    stay program, we were taken for bowling, swimming and to a theme park like Disneyland. Then on
                                     the final day we were taken to their national museum and then around the shopping malls where we
                                      did our shopping. The day ended with a dinner and a trip to a game arcade before going back to 
                                      their respective homes. Although most of our scouts had gone to sleep, some of us were given
                                       farewell parties by our hosts.
                              </p>




                              <p class="text-dark text-justify text-4">The next day we met at the airport where we bade farewell
                                 to our friends and their families who hosted us during our stay. The flight to Colombo included
                                  short stops in Hong Kong and Bangkok. We arrived in Colombo on the 17<sup>th</sup> of August, leaving us 
                                  one day of the 2<sup>nd</sup> term vacation to get ready for the first day of school.
                              </p>


                              <p class="text-dark text-justify text-4">We would like to thank the Sub Warden (Acting Warden), Mr. D. Edirisinghe,
                                 G.S.L. Mr. Victor and A.S.L. Beshan Kulapala for all their support and advice concerning the Jamboree.
                              </p>



                             
                           
                            
