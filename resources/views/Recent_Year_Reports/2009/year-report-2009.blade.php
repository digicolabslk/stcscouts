                              <p class="text-dark text-justify text-4">The year 2009 was an extremely successful period for
                                 the 16<sup>th</sup> Colombo Scout Troop. The Troop, managed to win all district events that they took part in. We also conducted several events.
                              </p>


                              <p class="text-dark text-justify text-4">The year commenced with a patrol leaders’ training camp. 
                                This was held from the 31<sup>st</sup> of January to the 1st of February 2009.
                              </p>


                              <p class="text-dark text-justify text-4">A patrol training camp was organized by the Troop in order
                                 to develop the standards of the Troop in preparation for the upcoming C.P. Jayawardena and Mayor’s Flag 
                                 competitions. This camp was carried out successfully from 18<sup>th</sup> to the 22<sup>nd</sup> of April 2009 at the Lee Dasanayake Training Camp Site in Mirigama. A total forty scouts attended this training camp.
                              </p>


                              <p class="text-dark text-justify text-4">The Colombo District organized a President’s Award training camp and 
                                several senior scouts attended this camp.



                              <p class="text-dark text-justify text-4">The troop organized “Wild Drift 2009”, a scout and guide cooking 
                                competition. This was carried out on the 23<sup>rd</sup> of May with three Guide troops attending. The Annual Colombo 
                                District Athletics meet was held on the 30<sup>th</sup> of May. All the scouts of 16<sup>th</sup> Colombo participated well, winning
                                 most of their events to finally become the overall Champions.
                              </p>




                              <p class="text-dark text-justify text-4">The 16<sup>th</sup> Colombo Scout Troop took part in the Brigadier
                                 C.P. Jayawardena Challenge Shield and the Mayor’s Flag Competitions for the best Senior and Junior
                                  Scout Troops in the Colombo District. Both teams put a great effort into training and succeeded in winning both competitions.
                              </p>



                              <p class="text-dark text-justify text-4">The 46<sup>th</sup> Colombo District Camporee was held from the 13<sup>th</sup> of August to the
                                 18<sup>th</sup> of August at the Vihara Maha Devi Park Colombo. The Troop managed to obtain pennants for all three days. 
                                 The Pennants were awarded for the maintenance of high camp standards at the Camporee.
                              </p>



                              <p class="text-dark text-justify text-4">The troop participated in the annual Colombo District Swimming Meet
                                 which was held on the 26<sup>th</sup> of September at the Nalanda College swimming pool. The scouts participated well
                                  and were adjudged the overall champions.
                              </p>




                              <p class="text-dark text-justify text-4">The annual community service project of the troop was held on the 31<sup>st</sup> of October 2009
                                 for the I.D.P. Children at the Shakthi Illam children’s home in Ratmalana.
                              </p>


                              <p class="text-dark text-justify text-4">The troop organized its annual Christmas programme on the 5<sup>th</sup> of December
                                 and also hosted the Colombo District Christmas programme.
                              </p>



                              <p class="text-dark text-justify text-4">To complete the year, the patrol leaders of the troop organized a patrol camp which
                                 was held from the 14<sup>th</sup> to the 17<sup>th</sup> of December in Salawa, Awisawella.
                              </p>



                              <p class="text-dark text-justify text-4">This year the troop produced three President’s Scouts, three Chief Commissioner’s Award winners, 
                                four District Commissioner’s Cord Winners, ten Scout Award winners and 25 new scouts were invested during the year.
                              </p>



                              <p class="text-dark text-justify text-4">Finally we would like to take this chance to extend our heartfelt gratitude to the group committee, the instructors and the parents of the
                                 scouts without whose cooperation and commitment, none of the above achievements would have been possible.
                              </p>


