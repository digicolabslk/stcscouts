                               <p class="text-dark text-justify text-4 text-start">
                             <span class="font-weight-bold" style=" color: black;">Term I </span>
                             </p>
                             



                              <p class="text-dark text-justify text-4">The year 2016 commenced on the 2<sup>nd</sup> of January with a
                                 Badgework workshop organized at the Scout room. Many badge examiners including Captain K. Jayathilaka
                                  attended this workshop and conducted sessions with regard to badges, estimation, pioneering, tracking
                                   and hiking. With this the Scouts were able to proceed well in their Badge work.
                              </p>


                              <p class="text-dark text-justify text-4">The Australian Scout Jamboree was held from the 
                                3<sup>rd</sup> – 13<sup>th</sup> January 2016 at Cataract Scout Park in New South Wales. J.S. Fernando, S.R. Jayasinghe and 
                                P. Ananth from our Troop took part in this Jamboree and they returned back with a lifetime memories
                              </p>


                              <p class="text-dark text-justify text-4">The Instructors of the Troop organized the Annual Seniors Training Camp from 
                                the 8<sup>th</sup>  – 9<sup>th</sup>  of January. A total of 21 Senior Scouts took part in this camp which took place at the
                                 Scout Room. Leadership, character, conduct, discipline, teamwork and unity were the key aspects which 
                                 this camp concentrated on building. This was an immense success and brought a strong unity among the
                                  Senior batch of Scouts.
                              </p>


                              <p class="text-dark text-justify text-4">The Group Committee Meeting and Dinner took place on the 21<sup>st</sup> of January by 
                                felicitating the new Sub warden Mr. Asanka Perera and the new Lower School Headmistress Mrs. LalanI
                                 Muthuthanthri.
                              </p>


                               <div style="color: black;">
                                 <p style="margin: 0; color: black;">The School Prize Giving for 2015 was held on the 3<sup>rd</sup> of February and the Scouting awards were won by the following Scouts.</p>
                                 <p style="margin: 0; color: black;"><span class="number">1.</span>The Arthur Abeysekera Memorial Trophy for Efficiency in Scouting : P.R. Wanigasooriya</p>
                                 <p style="margin: 0; color: black;"><span class="number">2.</span>The A.L. Victor Memorial prize for the Best Scout : A.Q.H. Muheed</p>
                                 <p style="margin: 0; color: black;"><span class="number">3.</span>Prize for the Best Junior Scout in Memory of Mr. W.H. Pereira : K.M.A.I. Kulatunga</p>
                                 <p style="margin: 0; color: black;"><span class="number">4.</span>The Samson Thenabadu Trophy for the Most Promising Junior Scout : A.F.A. Nishad</p>
                                 
                              </div>
                              <br>


                               <p class="text-dark text-justify text-4">The month of February was very important to the Troop as the 9<sup>th</sup> National Jamboree took
                                 place at Jaffna from the 20<sup>th</sup> – 27<sup>th</sup> of February in which 40 Scouts participated. A Jamboree is held
                                  once in four years and the Scouts got a unique opportunity to participate in this Jamboree. This was
                                   the first time in Sri Lankan Scouting history that a Jamboree was held in the Northern province. Weeks
                                    of planning and hard work was put in to participate in this camp. The Troop was awarded 2 pennants for
                                     outstanding performance.
                              </p>


                               <p class="text-dark text-justify text-4">As soon after the Jamboree the Scouts had the task to put up the “Scout Dunk” at
                                 the Rendezvous organized by the PTA on the 28<sup>th</sup> of February. The dunk was experienced by students,
                                  old boys and parents.
                              </p>


                               <p class="text-dark text-justify text-4">The Annual General Meeting of the Colombo District Scout Branch was held
                                 on the 31<sup>st</sup> March at Royal College. The Troop attended this event and received awards for what they
                                  achieved last year such as the Mayor’s Flag, Brig. C.P. Jayawardena Challenge Shield and Swimming Meet.
                              </p>


                               <p class="text-dark text-justify text-4">Next on the calendar was the Troop Night. Held on the 8<sup>th</sup> & 9<sup>th</sup> of 
                                April the Scouts actively took part in this camp. A fun filled camp was very well organized by the
                                 Senior Scouts and it helped all the Junior Scouts to get more involved in Scouting.
                              </p>


                               <p class="text-dark text-justify text-4">The event which was brought a conclusion for the term was
                                 the CPJ and the Mayors’ Flag Outing which took place on the 22<sup>nd</sup> & 23<sup>rd</sup> of April. The 2 teams who 
                                 emerged Champions in year 2015 were taken to the Army Staff Training Centre, Makola. The outing was
                                  very well organized by the Army. The Scouts who took part had a great and a wonderful time.
                              </p>


                              


                              <p class="text-dark text-justify text-4 text-start">
                             <span class="font-weight-bold" style=" color: black;">Term II </span>
                             </p>

                             



                              <p class="text-dark text-justify text-4">The Trinity term began with the Troop Movie out”
                                 held on the 7<sup>th</sup> of May. This is the first time an event of this sort was organized where the Cubs
                                  and Scouts were taken to the Savoy Cinema for the movie ‘The Jungle Book’.
                              </p>



                              <p class="text-dark text-justify text-4">The District Patrol Leaders’ Training Course
                                 took place on the 8th of May at Defense Service College, Colombo. D.R.K. Haththotuwa from our Troop
                                  participated at this training course. The Scouts who attended this camp gained many knowledge on
                                   leadership.
                              </p>




                              <p class="text-dark text-justify text-4">The Janashkthi Bravery Awards Ceremony was held on the
                                 9<sup>th</sup> of June at the B.M.I.C.H. Ten Senior Scouts represented College and had the unique opportunity
                                  of interacting with some of the award holders.
                              </p>


                              <p class="text-dark text-justify text-4">The investiture ceremony was held on the 11<sup>th</sup> of June and 11 recruits were 
                                invested as Scouts. The Scouts were given the World & National Scout badges, and the Troop scarf on 
                                this day.
                              </p>


                              <p class="text-dark text-justify text-4">The Sports Day was held on the 21<sup>st</sup> June at the Main hall. This event is organized
                                 every year by School to spread the values of each sport in school and to recruit many students as
                                  to their respective choice. The Troop once again put up a convincing stall for Scouting and recruited
                                   many students to join Scouting.
                              </p>



                              <p class="text-dark text-justify text-4">A Walk organized by the National Christian Council of Sri Lanka (NCCSL) to felicitate Miss
                                 Jayanthi Kuruthumpala and Mr. John Peiris on their achievement of climbing Mount Everest took place 
                                 on the 24<sup>th</sup> June. The walk commenced from Methodist College and went all the way up to Ladies’ College. 
                                 15 Senior Scouts participated in this walk and showed their appreciation to the achievement.
                              </p>


                              <p class="text-dark text-justify text-4">The next big event in the Term 2 calendar was the Scout Service & Relationship Week held
                                 from the 1<sup>st</sup> – 10<sup>th</sup> July. After two weeks of sweat and hard work the Troop once again emerged the
                                  “Highest Collectors of the Island” – an achievement that have continued to retain for the past few years. The breakdown of the collection are as follows,
                              </p>



