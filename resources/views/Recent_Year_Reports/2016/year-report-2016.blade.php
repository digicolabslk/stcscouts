                              <p class="text-dark text-justify text-4">The President Award Evaluation Camp was held from the 
                                21<sup>st</sup>  and 22<sup>nd</sup>  October at Kaduwela. W.M.P.A. Bandaratilleke and D.S. Udage from our Troop took part 
                                in this camp. They were trained on all the aspects in Scouting at this camp.
                              </p>


