                                recruited as Scouts on this day. The Senior Scouts organized a fun filled programme to the new recruits 
                                and they all had a wonderful time.
                              </p>



                              <p class="text-dark text-justify text-4">The Scouts once again put the famous “Scout Dunk” at the Thomian Ride 
