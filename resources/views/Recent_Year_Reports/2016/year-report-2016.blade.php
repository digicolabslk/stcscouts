                              <p class="text-dark text-justify text-4">Possibly the most important and final event of the term was the 51<sup>st</sup> 
                                Colombo Scout Camporee which was held from the 10<sup>th</sup> – 14<sup>th</sup> of August at Vihara Maha Devi Park. After a 
                                lapse four years we got the opportunity camp at Viharamahadevi Park. After weeks of planning and hard
                                 work the Troop was able to construct prominent camp gadgets, the kitchen and tower structures. The hard
                                  work put in by the Scouts was very much appreciated by everyone who visited our campsite. Further the
                                   Troop was able showcase high camping standards by winning both the pennants at the camp.
                              </p>



                              



                              <p class="text-dark text-justify text-4 text-start">
                             <span class="font-weight-bold" style=" color: black;">Term III </span>
                             </p>
                             <br>



                              <p class="text-dark text-justify text-4">The Michealmas term commenced with the much awaited District
                                 Competitions at which the troop has been triumphing for the past few years. The Brig. C.P. Jayawardena
                                  Challenge Shield awarded to the Best Senior Scout Troop of the District and the Mayors’ Flag for the
                                   Best Junior Scout Troop of the District were both held at Payagala, Kalutara on the 10<sup>th</sup> and 11<sup>th</sup> of
                                    September. After two months of dedication and commitment towards training both the teams emerged 
                                    champions. The Mayor’s Flag team emerged joint Champions with S. Thomas’ Preparatory School and 
                                    St. Joseph’s College for the second consecutive year. The Senior team emerged victorious and crowned
                                     as Champions for the 8<sup>th</sup> consecutive year with an 80-point margin beating many other Scout Troops.
                              </p>


