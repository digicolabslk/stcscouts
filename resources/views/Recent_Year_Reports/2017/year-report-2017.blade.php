                             </p>



                              <p class="text-dark text-justify text-4">The year 2017 commenced on the 21<sup>st</sup> of 
                                January with a with the New Office Bearers taking on the duties of the Troop for the year 2017.
                              </p>


                              <p class="text-dark text-justify text-4">The Australian Scout Jamboree was held from the 
                                3<sup>rd</sup> – 13<sup>th</sup> January 2016 at Cataract Scout Park in New South Wales. J.S. Fernando, S.R. Jayasinghe and 
                                P. Ananth from our Troop took part in this Jamboree and they returned back with a lifetime memories
                              </p>


                              <p class="text-dark text-justify text-4">The Seniors of the Troop organized the Annual Patrol Leaders Training Camp from
                                 the 8<sup>th</sup> – 9<sup>th</sup> of January. A total of 20 Scouts took part in this camp which took place at the Scout
                                  Room.  Leadership, character, conduct, discipline, teamwork and unity were the key aspects which this
                                   camp concentrated on building. This was an immense success and brought a strong leadership mentality
                                    among the group of Junior Scouts.
                              </p>


                              <p class="text-dark text-justify text-4">The School Prize Giving for 2017 was held on the 3<sup>rd</sup> of February and awards were won by the following Scouts.
