                                 <p style="margin: 0; color: black;"><span class="number">4.</span>The Samson Thenabadu Trophy for the Most Promising Junior Scout : A.F.A. Nishad</p>
                                 
                              </div>
                              <br>


                               <p class="text-dark text-justify text-4">The Presidents Evaluation Camp organized by the District was held from the 24<sup>th</sup> to the 26<sup>th</sup> of February.
                                 The Senior Scouts that Attended this camp gained the training and knowledge needed in order to become 
                                 a president scout.
                              </p>


                               <p class="text-dark text-justify text-4">Next event in the Troop calendar was the Troop Camp held from
                                 the 7<sup>th</sup> – 11<sup>th</sup> April at Welimada. A lot of hard work was put in by the Senior Scouts towards the 
                                 success of this camp. This Camp was a Training Camp for the Junior Scouts for Survival and Leadership
                                  and it was a very successful Training Camp. This Camp Molded the Junior Scouts to take Leadership and
                                   be confident in the Future.
                              </p>
                  

                        
                              <p class="text-dark text-justify text-4 text-start">
                             <span class="font-weight-bold" style="text-decoration: underline; color: black;">Term II </span>
                             </p>

                             



                              <p class="text-dark text-justify text-4">The District Patrol Leaders’ Training 
                                Course took place from the 26<sup>th</sup> to the 28<sup>th</sup> of  May at Defense Service College, Colombo. The Scouts who
                                 attended this camp gained knowledge in leadership.
                              </p>



                              <p class="text-dark text-justify text-4">The Investiture Ceremony was Held on the 10th of June where 11 scouts got invested by 
                                recieving the college scarf and Membership badges for the first time.
                              </p>




                              <p class="text-dark text-justify text-4">The next big event in the Term 2 calendar was the Scout Service &
                                 Relationship Week held from the 16<sup>th</sup> – 25<sup>th</sup> July. After two weeks of sweat and hard work the Troop once 
                                 again emerged the “Highest Collectors of the Island” – an achievement that have continued to retain for
                                  the past few years.
                              </p>


                              <p class="text-dark text-justify text-4">A Troop Night was organized on the 11<sup>th</sup> and 12<sup>th</sup> of August by the Senior Scouts
                                 at the scout room premises. This event was a success with the a participation of 50 Scouts. The scouts
                                  who participated in this program learned team work , leadership and logical thinking through this camp.
                              </p>

                             
                            

                             <p class="text-dark text-justify text-4 text-start">
                             <span class="font-weight-bold" style="text-decoration: underline; color: black;">Term III </span>
                             </p>



                              <p class="text-dark text-justify text-4">The Michealmas term commenced with the much awaited
                                 District Competitions at which the troop has been triumphing for the past few years. The Brig.
                                  C.P. Jayawardena Challenge Shield awarded to the Best Senior Scout Troop of the District and 
                                  the Mayors’ Flag for the best Junior Scout Troop of the District were both held at Payagala, 
                                  Kalutara on the 23<sup>rd</sup> and 24<sup>th</sup> of September in which the CPJ team emerged Champions while The 
                                  Mayor’s Flag team emerged 4<sup>th</sup> at this year’s Competition. The Senior team emerged victorious
                                   and crowned as Champions for the 10<sup>th</sup> consecutive year winning this years competition with a
                                    200 point margin beating many other Scout Troops.
                              </p>


