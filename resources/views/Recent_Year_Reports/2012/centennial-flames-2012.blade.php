                             <br>



                              <p class="text-dark text-justify text-4">2012 marks the centenary of the Sri Lankan
                                 branch of the biggest youth movement in this world at present, the World Scout Movement.
                                  Though Scouting itself was started in the year 1907, it was established in Sri Lanka five years 
                                  later, in 1912.
                              </p>


                              <p class="text-dark text-justify text-4">When we talk of Scouting at S. Thomas’ College, the records
                                 available suggest that since 1912 there were Boy Scouts in the school but to avoid conflict with
                                  Cadetting they were known as Junior Cadets. The records show that the proper Cub Pack was started 
                                  in 1922 by Mr. V.P. Cooke and that in the same year the first proper Scout Troop was started by 
                                  Mr. S.L.H. De Zylva and restarted in 1929 by Dr. R.L. Hayman and Mr. W.T. Keble. Then it is clear 
                                  that the Scout Group celebrates 90 years of official Scouting at S. Thomas’.
                              </p>


                              <p class="text-dark text-justify text-4">From there on Scouting continued in the school under the valuable
                                 guidance of Scout Leaders such as Dr. R.L. Hayman, Mr. W.I. Muttiah, Mr. N.B. Arputharaj, Mr. A.L. Victor,
                                  Mr. Daniel Edirisinghe, Mr. J. Dharmasena and Mrs. D.D. Sri Murugan who immensely have helped to bring 
                                  Scouting in the College to what it is, today.
                              </p>


                              <p class="text-dark text-justify text-4">Being acclaimed as one of the most active and prominent Scout troops
                                 in the island, it could be clearly seen that Scouting at S. Thomas’ College, Mount Lavinia has developed
                                  into a dynamic group in the school over the years from which productive citizens have been the fruitful
                                   product.
                               </p>


                              <p class="text-dark text-justify text-4">The 1990s, was a brilliant era for Scouting at S.T.C. as many projects
                                 were undertaken which were of a large scale. The Troop at this stage started doing projects along with
                                  Guides, who are their counterparts. The first project of this nature was “Beats of Scouting” in 1995
                                   which brought together fellow guide companies from the Colombo district. From there on various
                                    projects of similar nature were conducted. The peak of organizing such projects was when the
                                     Troop organized “Tribe Out” in 2002 which was a camp with the Guides. This went on to show the
                                      bonds and the trust that the two movements had built over the past years.
                              </p>




                              <p class="text-dark text-justify text-4">The troop has produced a fair number of President’s Scouts each
                                 year, thus proving that they are not only good in practical work but also in theory. At present
                                  the troop is supported by a very active Group Committee comprising of former scouts, parents 
                                  and well-wishers who support the Troop financially. The instructors meanwhile render a lot of
                                   help to the present scouts in their daily activities as well.
                              </p>



                              <p class="text-dark text-justify text-4">The scouts of S. Thomas’ Mount Lavinia have continuously
                                 contributed to Scouting in Sri Lanka over the past years by organizing unique projects which
                                  were of great value. The last major project of such nature carried out by the troop was in 2004 
                                  when it organized ‘Tribal Craft’, a huge scout exhibition which was not only a thumping financial 
                                  triumph but also an exhibition which put Thomian Scouts on the map as one of the most active and
                                   capable Troops in the island.
                              </p>



                              <p class="text-dark text-justify text-4">Many scouts from the Troop were able to represent Sri Lanka 
                                in a lot of international events as well over the past few years. The most recent venture of this 
                                calibre was the 22<sup>nd</sup> World Scout Jamboree held in Sweden where 4 scouts took part from our Troop.
                              </p>




                              <p class="text-dark text-justify text-4">With regard to taking part in the competitions organized by the district,
                                 the Troop has done exceptionally well over the past years. The scouts have been taking part in 
                                 the Mayor’s Flag competition which is for the junior scouts and in the Brig. C.P. Jayawardena 
                                 Challenge Trophy which evaluate the Scouting skills of the senior scouts in this regard, for many years.
                              </p>


                              <p class="text-dark text-justify text-4">In these two competitions held last year, the troop was able to be champions
                                 in both these competitions. This made the troop to retain the Mayor’s Flag for the 6<sup>th</sup> consecutive
                                  year and the C.P. Jayawardena shield for the 3<sup>rd</sup> consecutive year. When considering both the
                                   competitions as a whole the troop has been able to win both the competitions for 3 years in a 
                                   row from 2009-2011. Meanwhile in the recently started competition for the cub scouts, the C.M.P.
                                    Wanigathunga competition, the troop has been able to win all 5 competitions held since 2007.
                              </p>



                              <p class="text-dark text-justify text-4">Furthermore the Troop has been successful in the sports arena
                                 by winning the Sports Galas and the Swimming Meets organized by the District over the past few years.
                                  When talking of the financial successes that the Troop has enjoyed, the Troop has emerged the highest
                                   collectors in the Service and Relationship Week (a.k.a. Job Week) in the Colombo district for the
                                    past many years. All these facts indicate that the Thomian scout community has made its mark on 
                                    the whole movement in Sri Lanka.
                                </p>    



                              <p class="text-dark text-justify text-4">To celebrate 100 years of Scouting in our country, the Thomian 
                                scouts have organized a campfire in the hope that this would be yet another experience with a
                                 difference, for all participants and spectators. This project titled ‘CENTENNIAL FLAMES 2012’
                                  will be of a magnitude so as to revive the phenomenon of organizing large-scale projects. The 
                                  project is  organized with the main objective of creating a better understanding between scouts
                                   and society & increasing our bonds with our fellow scouts and guides while showcasing the talents 
                                   of everyone involved. The main theme behind the project is “Celebrating 100 years of selfless 
                                   service…”
                              </p>

                                     <p class="text-dark text-justify text-4">We expect the general public also to attend this 
                                        event which will not only promote Scouting but will also go on to make a contribution
                                         to the scout movement of Sri Lanka.
                              </p>


                                     <p class="text-dark text-justify text-4">The scouts hope to make this event a success which would 
                                        epitomize the strength of the Scouts at S. Thomas’, proving yet again what Thomian Scouts truly 
                                        stand for – uniqueness, skill and perfection.
                              </p>


